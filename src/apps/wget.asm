; ======================================================
; WGET for Sprinter DSS Network Kit
; Minimal HTTP/1.0 downloader over ESP-AT TCP.
; ======================================================

EXE_VERSION		EQU 1
DEFAULT_TIMEOUT		EQU 2000
RECV_TIMEOUT		EQU 7000
CLOSED_DRAIN_TIMEOUT	EQU 1500
TCP_OPEN_RETRY_DELAY	EQU 500
RECV_BUFFER_SIZE	EQU 16384
; Retain-tail margin: once the announced body is within this many bytes of
; completion we stop pausing for DSS_WRITE and instead accumulate the final
; bytes in RECV_BUFFER, flushing only after the transfer ends. This keeps RTS
; asserted (ESP drained) across the connection close, so ESP-AT never discards
; queued-but-unsent +IPD data on FIN. Must be < RECV_BUFFER_SIZE and safely
; larger than the largest tail ESP can drop in one close (~1-2 KB observed).
HOLD_TAIL_MARGIN	EQU 8192
URL_SIZE		EQU 160
HOST_SIZE		EQU 96
PORT_SIZE		EQU 8
PATH_SIZE		EQU 128
FILE_SIZE		EQU 80
REQ_SIZE		EQU 384
HEADER_LINE_SIZE	EQU 80
REDIRECT_LIMIT		EQU 5
RESUME_LIMIT		EQU 5
DSS_CREATE_OVERWRITE	EQU 0x0A
NO_HANDLE		EQU 0xFF
HTTP_UNSUPPORTED_NONE	EQU 0
HTTP_UNSUPPORTED_REDIRECT EQU 1
HTTP_UNSUPPORTED_HTTPS	EQU 2
HTTP_UNSUPPORTED_CHUNKED EQU 3
HTTP_UNSUPPORTED_GZIP	EQU 4

	DEVICE NOSLOT64K

	INCLUDE "macro.inc"
	INCLUDE "dss.inc"

	MODULE MAIN

	; Load this utility at 0x4100 so all code+BSS+stack live below
	; 0xC000, well clear of the ISA banking window. Long command lines
	; live at load_addr-0x80 = 0x4080.
LOAD_ADDR	EQU 0x4100
; Cmdline buffer pointer is taken from IX at entry (see START); load-#80
; (=0x4080) is the documented default but not assumed.
STACK_TOP	EQU 0x8000
WIN2_BASE	EQU 0x8000

	; Full 512-byte DSS EXE header.
	ORG LOAD_ADDR - 0x0200

EXE_HEADER
	DB "EXE"
	DB EXE_VERSION
	DW 0x0200
	DW 0
	DW 0
	DW 0
	DW 0
	DW 0
	DW START			; load address (offset 16)
	DW START			; entry point  (offset 18)
	DW STACK_TOP
	DS 490, 0

	ORG LOAD_ADDR

START
	; DSS passes the command-line buffer pointer in IX at entry
	; ([IX+0]=length, [IX+1..]=text). Capture it first, before any CALL clobbers
	; IX, instead of assuming the load-#80 address. CMDLINE_PTR is a code-segment
	; var, so CLEAR_BSS (WIN2) does not wipe it.
	LD	(CMDLINE_PTR),IX
	CALL	INIT_RUNTIME_PAGE
	JP	C,INIT_MEMORY_ERROR
	CALL	CLEAR_BSS
	LD	A,NO_HANDLE
	LD	(OUT_FH),A
	XOR	A
	LD	(TCP_IS_OPEN),A

	CALL	ISA.ISA_RESET
	CALL	WCOMMON.INIT_VMODE
	PRINTLN MSG_START

	CALL	PARSE_CMD_LINE
	JP	C,USAGE
	LD	A,(HELP_REQUESTED)
	AND	A
	JP	NZ,SHOW_HELP
	CALL	PARSE_URL
	JP	C,USAGE
	CALL	ENSURE_OUTPUT_FILE
	JP	C,USAGE
	CALL	SET_PORT_FLAG
	CALL	PRINT_SOURCE_INFO

	CALL	OPEN_OUTPUT_FILE
	JP	C,FILE_ERROR_EXIT

	CALL	WIFI.UART_FIND
	JP	C,NO_WIFI
	CALL	WCOMMON.REQUIRE_NET_UP

	CALL	NETCFG.LOAD
	CALL	NETCFG.APPLY_UART_BAUD
	CALL	WIFI.UART_INIT
	PRINTLN MSG_UART_READY

	LD	HL,CMD_AT
	CALL	SEND_CMD_RECOVER

	LD	HL,CMD_ECHO_OFF
	CALL	SEND_CMD

	; Force ESP-AT to enable hardware RTS/CTS flow control. Without this,
	; ESP keeps streaming bytes during slow DSS_WRITE/file ops and the Z80
	; UART FIFO overruns (OE in LSR), causing +IPD parser desync and the
	; body-on-screen / Network error #0 / #4 symptoms.
	CALL	WCOMMON.SETUP_UART_FLOW
	AND	A
	JP	NZ,TCP_ERROR_EXIT

	LD	HL,CMD_CIPMUX_0
	CALL	SEND_CMD

	CALL	TPUT.START			; start the transfer timer
	; Remember the starting offset (resume-prompt seed, or 0) so the final
	; speed covers all bytes fetched THIS run across every fragment/redirect,
	; not just the last fragment.
	LD	HL,(TOTAL_BYTES)
	LD	(SESSION_BASE),HL
	LD	HL,(TOTAL_BYTES+2)
	LD	(SESSION_BASE+2),HL

.REQUEST_LOOP
	PRINT MSG_CONNECTING
	PRINT HOST_BUFF
	LD	A,(PORT_IS_DEFAULT)
	AND	A
	JR	NZ,.CONNECT_DEFAULT_PORT
	PRINT MSG_COLON
	PRINT PORT_BUFF
.CONNECT_DEFAULT_PORT
	PRINT WCOMMON.LINE_END
	LD	HL,HOST_BUFF
	LD	DE,PORT_BUFF
	CALL	OPEN_TCP_RECOVER
	JP	C,TCP_ERROR_EXIT
	LD	A,1
	LD	(TCP_IS_OPEN),A

	CALL	BUILD_HTTP_REQUEST
	LD	HL,REQ_BUFF
	CALL	UTIL.STRLEN
	; A fast server can start sending the HTTP response before ESP prints
	; SEND OK. Do not wait here: RECEIVE_HTTP scans past SEND OK text and
	; keeps the first +IPD payload instead of discarding it in WAIT_SEND_OK.
	CALL	TCP.SEND_BUFFER_NO_WAIT
	JP	C,TCP_ERROR_EXIT

	CALL	RECEIVE_HTTP
	PUSH	AF
	CALL	WIFI.UART_RX_RESUME
	POP	AF
	JR	NC,.RECEIVE_OK
	LD	A,(HTTP_INCOMPLETE)
	AND	A
	JP	Z,TCP_ERROR_EXIT
	CALL	PREPARE_RESUME_RETRY
	JP	C,TCP_ERROR_EXIT
	JR	.REQUEST_LOOP
.RECEIVE_OK

	; If RECEIVE_HTTP saw the remote close the connection, the ESP socket
	; is already gone. Skip CIPCLOSE — it would just sit waiting for an
	; OK/ERROR that the firmware may take a long time to deliver.
	LD	A,(SAW_CLOSED)
	AND	A
	JR	NZ,.SKIP_CLOSE
	LD	A,(LAST_TCP_ERR)
	CP	RES_NOT_CONN
	JR	Z,.SKIP_CLOSE
	CALL	TCP.CLOSE
.SKIP_CLOSE
	XOR	A
	LD	(TCP_IS_OPEN),A
	LD	A,(REDIRECT_PENDING)
	AND	A
	JR	Z,.REQUEST_DONE
	CALL	FOLLOW_REDIRECT
	JP	C,HTTP_ERROR_EXIT
	JR	.REQUEST_LOOP

.REQUEST_DONE
	CALL	CLOSE_OUTPUT_FILE
	JP	C,FILE_ERROR_EXIT

	PRINT WCOMMON.LINE_END
	CALL	PRINT_DOWNLOADED_BYTES
	; Report speed over ALL bytes fetched this run (TOTAL_BYTES - SESSION_BASE),
	; summed across every fragment/redirect, not just the last fragment.
	LD	HL,(TOTAL_BYTES)
	LD	DE,(SESSION_BASE)
	OR	A
	SBC	HL,DE			; HL = low16 of (TOTAL - base)
	EX	DE,HL			; DE = low16
	LD	HL,(TOTAL_BYTES+2)
	LD	BC,(SESSION_BASE+2)
	SBC	HL,BC			; HL = high16 (borrow chained)
	EX	DE,HL			; HL = low16, DE = high16
	CALL	TPUT.REPORT
	PRINTLN MSG_DONE
	LD	B,0
	JP	WCOMMON.EXIT

NO_WIFI
	CALL	CLOSE_OUTPUT_FILE
	PRINTLN MSG_WIFI_NOT_FOUND
	LD	B,2
	JP	WCOMMON.EXIT

USAGE
	PRINTLN MSG_USAGE
	LD	B,1
	JP	WCOMMON.EXIT

SHOW_HELP
	PRINTLN MSG_USAGE
	LD	B,0
	JP	WCOMMON.EXIT

TCP_ERROR_EXIT
	; Save inbound error code BEFORE reading CANCELLED — otherwise A is
	; clobbered and every network error gets printed as #0.
	PUSH	AF
	LD	A,(WCOMMON.CANCELLED)
	AND	A
	JR	Z,.NOT_CANCELLED
	POP	AF
	JP	CANCEL_EXIT
.NOT_CANCELLED
	LD	A,(TCP_IS_OPEN)
	AND	A
	CALL	NZ,TCP.CLOSE
	CALL	CLOSE_OUTPUT_FILE
	POP	AF
	ADD	A,'0'
	LD	(MSG_ERROR_NO),A
	PRINTLN MSG_NET_ERROR
	LD	B,3
	JP	WCOMMON.EXIT

FILE_ERROR_EXIT
	LD	A,(OUTPUT_ABORTED)
	AND	A
	JR	NZ,OUTPUT_ABORT_EXIT
	CALL	PRINT_FILE_ERROR
	LD	B,5
	JP	WCOMMON.EXIT

OUTPUT_ABORT_EXIT
	PRINTLN	MSG_ABORTED
	LD	B,1
	JP	WCOMMON.EXIT

HTTP_ERROR_EXIT
	LD	A,(TCP_IS_OPEN)
	AND	A
	CALL	NZ,TCP.CLOSE
	CALL	CLOSE_OUTPUT_FILE
	CALL	PRINT_HTTP_STATUS
	CALL	PRINT_HTTP_UNSUPPORTED
	LD	B,3
	JP	WCOMMON.EXIT

CANCEL_EXIT
	LD	A,(TCP_IS_OPEN)
	AND	A
	CALL	NZ,TCP.CLOSE
	CALL	CLOSE_OUTPUT_FILE
	PRINT	WCOMMON.LINE_END
	PRINTLN	MSG_CANCELLED
	LD	B,7
	JP	WCOMMON.EXIT

; ------------------------------------------------------
; Parse command line:
;   WGET.EXE url [-o output] [-y|-f overwrite] [-r resume]
;   WGET.EXE url FILE              ; legacy compatible alias for -o FILE
; ------------------------------------------------------
PARSE_CMD_LINE
	XOR	A
	LD	(HELP_REQUESTED),A
	LD	(FORCE_OVERWRITE),A
	LD	HL,(CMDLINE_PTR)
	LD	A,(HL)
	AND	A
	JP	Z,.ERR
	LD	B,A
	INC	HL
	CALL	SKIP_SPACES
	JP	C,.ERR
	LD	DE,URL_BUFF
	LD	C,URL_SIZE-1
	CALL	COPY_ARG
	JR	C,.ERR

	PUSH	HL
	PUSH	BC
	CALL	IS_HELP_TOKEN_URL
	POP	BC
	POP	HL
	JR	NC,.HELP
.NEXT_OPT
	CALL	SKIP_SPACES
	JR	C,.DONE
	LD	DE,HOST_BUFF
	LD	C,FILE_SIZE-1
	CALL	COPY_ARG
	JR	C,.ERR
	PUSH	HL
	PUSH	BC
	CALL	IS_OPT_OUTPUT
	POP	BC
	POP	HL
	JR	NC,.OUTPUT
	PUSH	HL
	PUSH	BC
	CALL	IS_OPT_YES
	POP	BC
	POP	HL
	JR	NC,.YES
	PUSH	HL
	PUSH	BC
	CALL	IS_OPT_RESUME
	POP	BC
	POP	HL
	JR	NC,.RESUME_OPT
	PUSH	HL
	PUSH	BC
	CALL	IS_HELP_TOKEN_TMP
	POP	BC
	POP	HL
	JR	NC,.HELP
	LD	A,(OUT_FILE)
	AND	A
	JR	NZ,.ERR
	; COPY_ASCIIZ_LIMIT clobbers HL/BC, which still hold the cmdline cursor and
	; remaining-length. Preserve them so a flag after a positional output name
	; (e.g. `WGET url OUT -y`) is not read from garbage and lost.
	PUSH	HL
	PUSH	BC
	LD	HL,HOST_BUFF
	LD	DE,OUT_FILE
	LD	C,FILE_SIZE-1
	CALL	COPY_ASCIIZ_LIMIT
	POP	BC
	POP	HL
	JR	NC,.NEXT_OPT
	JR	.ERR
.OUTPUT
	CALL	SKIP_SPACES
	JR	C,.ERR
	LD	DE,OUT_FILE
	LD	C,FILE_SIZE-1
	CALL	COPY_ARG
	JR	NC,.NEXT_OPT
	JR	.ERR
.YES
	LD	A,1
	LD	(FORCE_OVERWRITE),A
	JR	.NEXT_OPT
.RESUME_OPT
	LD	A,1
	LD	(FORCE_RESUME),A
	JR	.NEXT_OPT
.HELP
	LD	A,1
	LD	(HELP_REQUESTED),A
	RET
.DONE
	AND	A
	RET
.ERR
	SCF
	RET

IS_HELP_TOKEN_URL
	LD	DE,URL_BUFF
	JR	IS_HELP_TOKEN

IS_HELP_TOKEN_TMP
	LD	DE,HOST_BUFF
IS_HELP_TOKEN
	LD	HL,SWITCH_HELP_Q_SLASH
	CALL	UTIL.STRCMP_CI
	RET	NC
	LD	HL,SWITCH_HELP_Q_DASH
	CALL	UTIL.STRCMP_CI
	RET	NC
	LD	HL,SWITCH_HELP_H_SLASH
	CALL	UTIL.STRCMP_CI
	RET	NC
	LD	HL,SWITCH_HELP_H_DASH
	JP	UTIL.STRCMP_CI

IS_OPT_OUTPUT
	LD	HL,SWITCH_OUTPUT_DASH
	LD	DE,HOST_BUFF
	CALL	UTIL.STRCMP_CI
	RET	NC
	LD	HL,SWITCH_OUTPUT_SLASH
	LD	DE,HOST_BUFF
	JP	UTIL.STRCMP_CI

; Overwrite flag: -y / /y, plus -f / /f as a clearer "force" alias.
IS_OPT_YES
	LD	HL,SWITCH_YES_DASH
	LD	DE,HOST_BUFF
	CALL	UTIL.STRCMP_CI
	RET	NC
	LD	HL,SWITCH_YES_SLASH
	LD	DE,HOST_BUFF
	CALL	UTIL.STRCMP_CI
	RET	NC
	LD	HL,SWITCH_FORCE_DASH
	LD	DE,HOST_BUFF
	CALL	UTIL.STRCMP_CI
	RET	NC
	LD	HL,SWITCH_FORCE_SLASH
	LD	DE,HOST_BUFF
	JP	UTIL.STRCMP_CI

; Resume flag: -r / /r (force append to an existing file, no prompt).
IS_OPT_RESUME
	LD	HL,SWITCH_RESUME_DASH
	LD	DE,HOST_BUFF
	CALL	UTIL.STRCMP_CI
	RET	NC
	LD	HL,SWITCH_RESUME_SLASH
	LD	DE,HOST_BUFF
	JP	UTIL.STRCMP_CI

SKIP_SPACES
	LD	A,B
	AND	A
	JR	Z,.ERR
	LD	A,(HL)
	CP	0x21
	RET	NC
	INC	HL
	DJNZ	SKIP_SPACES
.ERR
	SCF
	RET

COPY_ARG
	XOR	A
	LD	(ARG_LEN),A
.NEXT
	LD	A,B
	AND	A
	JR	Z,.END
	LD	A,(HL)
	CP	0x21
	JR	C,.END
	LD	A,C
	AND	A
	JR	Z,.ERR
	LD	A,(HL)
	LD	(DE),A
	INC	DE
	INC	HL
	DEC	B
	DEC	C
	LD	A,(ARG_LEN)
	INC	A
	LD	(ARG_LEN),A
	JR	.NEXT
.END
	XOR	A
	LD	(DE),A
	LD	A,(ARG_LEN)
	AND	A
	RET	NZ
.ERR
	SCF
	RET

; ------------------------------------------------------
; Parse URL_BUFF into HOST_BUFF, PORT_BUFF and PATH_BUFF.
; ------------------------------------------------------
PARSE_URL
	XOR	A
	LD	(URL_SCHEME_ADDED),A
	LD	HL,URL_BUFF
	LD	DE,HTTP_PREFIX
	CALL	UTIL.STARTSWITH
	JR	Z,.HAS_HTTP
	LD	A,1
	LD	(URL_SCHEME_ADDED),A
	LD	HL,URL_BUFF
	JR	.PARSE_HOST
.HAS_HTTP
	LD	BC,7
	ADD	HL,BC

.PARSE_HOST
	LD	DE,HOST_BUFF
	LD	C,HOST_SIZE-1
.HOST
	LD	A,(HL)
	AND	A
	JR	Z,.HOST_DONE
	CP	'/'
	JR	Z,.HOST_DONE
	CP	':'
	JR	Z,.PORT_START
	LD	A,C
	AND	A
	JR	Z,.ERR
	LD	A,(HL)
	LD	(DE),A
	INC	DE
	INC	HL
	DEC	C
	JR	.HOST

.HOST_DONE
	XOR	A
	LD	(DE),A
	LD	A,(HOST_BUFF)
	AND	A
	JR	Z,.ERR
	PUSH	HL
	LD	DE,DEFAULT_PORT
	CALL	COPY_PORT_DEFAULT
	POP	HL
	JR	.PATH_START

.PORT_START
	XOR	A
	LD	(DE),A
	INC	HL
	LD	DE,PORT_BUFF
	LD	C,PORT_SIZE-1
.PORT
	LD	A,(HL)
	AND	A
	JR	Z,.PORT_DONE
	CP	'/'
	JR	Z,.PORT_DONE
	CP	'0'
	JR	C,.ERR
	CP	'9'+1
	JR	NC,.ERR
	LD	A,C
	AND	A
	JR	Z,.ERR
	LD	A,(HL)
	LD	(DE),A
	INC	DE
	INC	HL
	DEC	C
	JR	.PORT
.PORT_DONE
	XOR	A
	LD	(DE),A
	LD	A,(PORT_BUFF)
	AND	A
	JR	Z,.ERR

.PATH_START
	LD	A,(HL)
	CP	'/'
	JR	Z,.COPY_PATH
	LD	HL,DEFAULT_PATH
	LD	DE,PATH_BUFF
	JP	COPY_ASCIIZ_DE

.COPY_PATH
	LD	DE,PATH_BUFF
	LD	C,PATH_SIZE-1
.PATH
	LD	A,(HL)
	AND	A
	JR	Z,.PATH_DONE
	LD	A,C
	AND	A
	JR	Z,.ERR
	LD	A,(HL)
	LD	(DE),A
	INC	DE
	INC	HL
	DEC	C
	JR	.PATH
.PATH_DONE
	XOR	A
	LD	(DE),A
	RET
.ERR
	SCF
	RET

COPY_PORT_DEFAULT
	LD	HL,DEFAULT_PORT
	LD	DE,PORT_BUFF
	JP	COPY_ASCIIZ_DE

COPY_ASCIIZ_DE
	LD	A,(HL)
	LD	(DE),A
	AND	A
	RET	Z
	INC	HL
	INC	DE
	JR	COPY_ASCIIZ_DE

COPY_ASCIIZ_LIMIT
	LD	A,(HL)
	AND	A
	JR	Z,.END
	LD	A,C
	AND	A
	JR	Z,.ERR
	LD	A,(HL)
	LD	(DE),A
	INC	HL
	INC	DE
	DEC	C
	JR	COPY_ASCIIZ_LIMIT
.END
	XOR	A
	LD	(DE),A
	RET
.ERR
	SCF
	RET

ENSURE_OUTPUT_FILE
	LD	A,(OUT_FILE)
	AND	A
	RET	NZ
	LD	IX,PATH_BUFF
	LD	HL,PATH_BUFF
.SCAN
	LD	A,(HL)
	AND	A
	JR	Z,.FOUND
	CP	'/'
	JR	Z,.SLASH
	CP	'\'
	JR	Z,.SLASH
	INC	HL
	JR	.SCAN
.SLASH
	INC	HL
	PUSH	HL
	POP	IX
	JR	.SCAN
.FOUND
	LD	A,(IX+0)
	AND	A
	JR	Z,.DEFAULT
	PUSH	IX
	POP	HL
	LD	DE,OUT_FILE
	LD	C,FILE_SIZE-1
	JP	COPY_ASCIIZ_LIMIT
.DEFAULT
	LD	HL,DEFAULT_OUT_FILE
	LD	DE,OUT_FILE
	LD	C,FILE_SIZE-1
	JP	COPY_ASCIIZ_LIMIT

; ------------------------------------------------------
; Print source URL details after parsing.
; ------------------------------------------------------
PRINT_SOURCE_INFO
	PRINT	MSG_SOURCE
	PRINT	URL_BUFF
	PRINT	WCOMMON.LINE_END
	LD	A,(URL_SCHEME_ADDED)
	AND	A
	JR	Z,.NO_WARN
	PRINTLN	MSG_SCHEME_ADDED
.NO_WARN
	RET

; ------------------------------------------------------
; Clear runtime-only buffers placed after the loaded EXE image.
; ------------------------------------------------------
INIT_RUNTIME_PAGE
	LD	B,1
	LD	C,DSS_GETMEM
	RST	DSS
	RET	C
	LD	B,0
	LD	C,DSS_SETWIN2
	RST	DSS
	RET

INIT_MEMORY_ERROR
	LD	B,3
	LD	C,DSS_EXIT
	RST	DSS

CLEAR_BSS
	LD	HL,WGET_BSS_BASE
	LD	DE,WGET_BSS_BASE+1
	LD	BC,WGET_BSS_END-WGET_BSS_BASE-1
	LD	(HL),0
	LDIR
	RET

; ------------------------------------------------------
; Local output file helpers.
; ------------------------------------------------------
OPEN_OUTPUT_FILE
	PRINT MSG_OUTPUT
	PRINT OUT_FILE
	PRINT WCOMMON.LINE_END
	LD	HL,OUT_FILE
	LD	A,FA_READONLY
	LD	C,DSS_OPEN_FILE
	RST	DSS
	JR	C,.CREATE			; does not exist -> fresh download
	LD	C,DSS_CLOSE_FILE
	RST	DSS				; close the existence-test handle
	LD	A,(FORCE_OVERWRITE)
	AND	A
	JR	NZ,.DELETE			; -y/-f forces overwrite
	LD	A,(FORCE_RESUME)
	AND	A
	JR	NZ,.RESUME			; -r forces resume (append)
	CALL	CONFIRM_EXISTING		; A = 'R' resume / 'O' overwrite / 'C' cancel
	CP	'R'
	JR	Z,.RESUME
	CP	'O'
	JR	Z,.DELETE
	JR	.ABORT				; cancel
.RESUME
	; Reopen the existing file read/write and seek to its end so the new bytes
	; append; seed TOTAL_BYTES with the current size so BUILD_HTTP_REQUEST asks
	; for "Range: bytes=<size>-" and the server resumes from there.
	LD	HL,OUT_FILE
	LD	A,0				; FileMode RW
	LD	C,DSS_OPEN_FILE
	RST	DSS
	JR	C,.CREATE			; cannot reopen -> fall back to fresh
	LD	(OUT_FH),A
	LD	A,(OUT_FH)
	LD	B,SEEK_END
	LD	HL,0
	LD	IX,0
	LD	C,DSS_MOVE_FP
	RST	DSS				; HL:IX = size (HL high, IX low)
	LD	(TOTAL_BYTES),IX
	LD	(TOTAL_BYTES+2),HL
	XOR	A
	LD	(OUTPUT_ABORTED),A
	PRINT	MSG_RESUME_AT
	LD	HL,TOTAL_BYTES
	CALL	PRINT_U32_AT_HL
	PRINTLN	MSG_BYTES
	RET
.DELETE
	LD	HL,OUT_FILE
	LD	C,DSS_DELETE
	RST	DSS
.CREATE
	LD	HL,OUT_FILE
	LD	A,FA_ARCHIVE
	LD	C,DSS_CREATE_OVERWRITE
	RST	DSS
	RET	C
	LD	(OUT_FH),A
	XOR	A
	LD	(OUTPUT_ABORTED),A
	RET
.ABORT
	LD	A,1
	LD	(OUTPUT_ABORTED),A
	SCF
	RET

; Ask what to do about an existing output file. Returns A = 'R' (resume),
; 'O' (overwrite) or 'C' (cancel). Y=overwrite, N/Esc=cancel for familiarity.
CONFIRM_EXISTING
	PRINT	MSG_OVERWRITE_PRE
	PRINT	OUT_FILE
	PRINT	MSG_OVERWRITE_POST
.ASK
	; Drop the Enter that launched the command (and any other stale keys)
	; so WAITKEY blocks for a fresh choice instead of consuming the leftover.
	LD	C,DSS_KCLEAR
	RST	DSS
	LD	C,DSS_WAITKEY
	RST	DSS
	PUSH	AF
	LD	C,DSS_PUTCHAR
	RST	DSS
	LD	A,13
	LD	C,DSS_PUTCHAR
	RST	DSS
	LD	A,10
	LD	C,DSS_PUTCHAR
	RST	DSS
	POP	AF
	CP	'R'
	JR	Z,.RES
	CP	'r'
	JR	Z,.RES
	CP	'O'
	JR	Z,.OVR
	CP	'o'
	JR	Z,.OVR
	CP	'Y'
	JR	Z,.OVR
	CP	'y'
	JR	Z,.OVR
	CP	'C'
	JR	Z,.CAN
	CP	'c'
	JR	Z,.CAN
	CP	'N'
	JR	Z,.CAN
	CP	'n'
	JR	Z,.CAN
	CP	0x1B				; Esc
	JR	Z,.CAN
	JR	.ASK				; unrecognised -> ask again
.RES
	LD	A,'R'
	RET
.OVR
	LD	A,'O'
	RET
.CAN
	LD	A,'C'
	RET

CLOSE_OUTPUT_FILE
	LD	A,(OUT_FH)
	CP	NO_HANDLE
	RET	Z
	LD	C,DSS_CLOSE_FILE
	RST	DSS
	LD	A,NO_HANDLE
	LD	(OUT_FH),A
	RET

WRITE_OUTPUT
	LD	A,B
	OR	C
	RET	Z
	LD	A,(OUT_FH)
	LD	D,B
	LD	E,C
	LD	C,DSS_WRITE
	RST	DSS
	RET

PRINT_FILE_ERROR
	PRINT	MSG_FILE_ERROR
	CALL	DSS_ERROR.GET_ERR_MSG
	PRINTLN_HL
	RET

; ------------------------------------------------------
; ESP/TCP command helpers.
; ------------------------------------------------------
SEND_CMD
	LD	DE,WIFI.RS_BUFF
	LD	BC,DEFAULT_TIMEOUT
	CALL	WIFI.UART_TX_CMD
	AND	A
	RET	Z
	JP	TCP_ERROR_EXIT

SEND_CMD_RECOVER
	PUSH	HL
	LD	DE,WIFI.RS_BUFF
	LD	BC,DEFAULT_TIMEOUT
	CALL	WIFI.UART_TX_CMD
	AND	A
	JR	Z,.OK

	PRINTLN MSG_RESETTING_ESP
	CALL	WIFI.ESP_RESET
	CALL	WIFI.UART_SET_DEFAULT_DIVISOR
	CALL	WIFI.UART_INIT
	POP	HL
	JP	SEND_CMD
.OK
	POP	HL
	RET

PREPARE_TCP_OPEN
	CALL	WIFI.UART_RX_RESUME
	; Kill any stale single-connection socket left by a previous run or by
	; a FIN-time ESP desync. Ignore ERROR: it only means no socket was open.
	CALL	TCP.CLOSE
	XOR	A
	LD	(TCP_IS_OPEN),A
	LD	HL,0
	LD	(TCP.PAYLOAD_LEFT),HL
	XOR	A
	LD	(TCP.LSR_ACCUM),A
	CALL	WIFI.UART_EMPTY_RS
	LD	HL,CMD_AT
	CALL	SEND_CMD
	LD	HL,CMD_CIPMUX_0
	JP	SEND_CMD

OPEN_TCP_RECOVER
	PUSH	HL
	PUSH	DE
	CALL	PREPARE_TCP_OPEN
	POP	DE
	POP	HL
	CALL	TCP.OPEN
	RET	NC
	PUSH	AF
	CALL	WIFI.UART_RX_RESUME
	CALL	TCP.CLOSE
	XOR	A
	LD	(TCP_IS_OPEN),A
	LD	HL,0
	LD	(TCP.PAYLOAD_LEFT),HL
	XOR	A
	LD	(TCP.LSR_ACCUM),A
	CALL	WIFI.UART_EMPTY_RS
	LD	HL,TCP_OPEN_RETRY_DELAY
	CALL	UTIL.DELAY
	LD	HL,CMD_AT
	CALL	SEND_CMD
	LD	HL,CMD_CIPMUX_0
	CALL	SEND_CMD
	POP	AF
	LD	HL,HOST_BUFF
	LD	DE,PORT_BUFF
	JP	TCP.OPEN

; ------------------------------------------------------
; Build HTTP/1.0 GET request in REQ_BUFF.
; ------------------------------------------------------
BUILD_HTTP_REQUEST
	CALL	SET_PORT_FLAG
	LD	HL,REQ_BUFF
	XOR	A
	LD	(RANGE_ACTIVE),A
	LD	DE,REQ_GET
	CALL	APPEND_STR
	LD	IX,PATH_BUFF
	CALL	APPEND_IX_STR
	LD	DE,REQ_HTTP
	CALL	APPEND_STR
	LD	IX,HOST_BUFF
	CALL	APPEND_IX_STR
	LD	A,(PORT_IS_DEFAULT)
	AND	A
	JR	NZ,.NO_HOST_PORT
	LD	DE,REQ_PORT_SEP
	CALL	APPEND_STR
	LD	IX,PORT_BUFF
	CALL	APPEND_IX_STR
.NO_HOST_PORT
	CALL	APPEND_RANGE_HEADER_IF_NEEDED
	LD	DE,REQ_END
	JP	APPEND_STR

APPEND_RANGE_HEADER_IF_NEEDED
	CALL	TOTAL_BYTES_IS_ZERO
	RET	Z
	LD	A,1
	LD	(RANGE_ACTIVE),A
	LD	DE,REQ_RANGE
	CALL	APPEND_STR
	LD	DE,TOTAL_BYTES
	CALL	APPEND_U32_FROM_DE
	LD	DE,REQ_RANGE_END
	JP	APPEND_STR

; ------------------------------------------------------
; Set PORT_IS_DEFAULT=1 when port is "80", otherwise 0.
; ------------------------------------------------------
SET_PORT_FLAG
	LD	HL,DEFAULT_PORT
	LD	DE,PORT_BUFF
	CALL	UTIL.STRCMP
	JR	C,.CUSTOM
	LD	A,1
	LD	(PORT_IS_DEFAULT),A
	RET
.CUSTOM
	XOR	A
	LD	(PORT_IS_DEFAULT),A
	RET

; ------------------------------------------------------
; Receive HTTP response, skip headers and write body to file.
; ------------------------------------------------------
RECEIVE_HTTP
	XOR	A
	LD	(GOT_RESPONSE),A
	LD	(HEADER_DONE),A
	LD	(HDR_STATE),A
	LD	(HAVE_BODY),A
	LD	(HTTP_STATUS_PTR),A
	LD	(HTTP_STATUS_PTR+1),A
	LD	(ADJUST_RESPONSE),A
	LD	(CONTENT_LENGTH),A
	LD	(CONTENT_LENGTH+1),A
	LD	(CONTENT_LENGTH+2),A
	LD	(CONTENT_LENGTH+3),A
	LD	(CONTENT_LENGTH_SEEN),A
	LD	(BODY_BYTES),A
	LD	(BODY_BYTES+1),A
	LD	(BODY_BYTES+2),A
	LD	(BODY_BYTES+3),A
	LD	(HTTP_INCOMPLETE),A
	LD	(BODY_LOG_PRINTED),A
	LD	(DEBUG_PRINTED),A
	LD	(HEADER_LINE_LEN),A
	LD	(SAW_CLOSED),A
	LD	(HTTP_STATUS_CLASS),A
	LD	(REDIRECT_PENDING),A
	LD	(LOCATION_SEEN),A
	LD	(HTTP_UNSUPPORTED_REASON),A
	LD	(HOLD_MODE),A
	LD	(HOLD_LEN),A
	LD	(HOLD_LEN+1),A

.LOOP
	CALL	WCOMMON.CHECK_CANCEL
	JP	C,.CANCELLED
		CALL	WIFI.UART_RX_RESUME
		; Switch to retain-tail mode before the read once the body is within
		; HOLD_TAIL_MARGIN of completion; SETUP_RECV_DEST then accumulates into
		; RECV_BUFFER at a growing offset instead of overwriting from the start.
		CALL	UPDATE_HOLD_MODE
		CALL	SETUP_RECV_DEST
		LD	A,(SAW_CLOSED)
		AND	A
		JR	NZ,.AFTER_CLOSE_TIMEOUT
		LD	DE,RECV_TIMEOUT
		JR	.RECV
.AFTER_CLOSE_TIMEOUT
		LD	DE,CLOSED_DRAIN_TIMEOUT
.RECV
		LD	HL,(RECV_DEST)
		LD	BC,(RECV_CAP)
		CALL	TCP.RECEIVE
	; Pause RX immediately so ESP stops sending while we run the (slow)
	; HTTP parser, debug print, file write etc. Resumed at .LOOP top.
	PUSH	AF,BC
	CALL	WIFI.UART_RX_PAUSE
	CALL	STORE_RECV_DEBUG
	POP	BC,AF
	JP	C,.RX_ERROR
	CALL	UART_RX_ERROR_SEEN
	JP	NZ,.RX_UART_ERROR
	LD	A,B
	OR	C
	JR	Z,.RX_DONE
	CALL	TRIM_CLOSED_SUFFIX
	LD	A,B
	OR	C
	JR	Z,.RX_DONE

	PUSH	BC
	LD	A,(GOT_RESPONSE)
	AND	A
	JR	NZ,.STATUS_DONE
	LD	A,1
	LD	(GOT_RESPONSE),A
	LD	HL,RECV_BUFFER
	CALL	CHECK_HTTP_STATUS
	JR	C,.HTTP_ERR_POP
	LD	A,1
	LD	(ADJUST_RESPONSE),A
.STATUS_DONE
	POP	BC
	LD	A,(ADJUST_RESPONSE)
	AND	A
	JR	Z,.PROCESS_FROM_START
	XOR	A
	LD	(ADJUST_RESPONSE),A
	CALL	ADJUST_TO_HTTP_STATUS
	JR	.PROCESS_DATA
.PROCESS_FROM_START
	LD	HL,(RECV_DEST)
.PROCESS_DATA
		CALL	PROCESS_HTTP_DATA
		JR	C,.PROCESS_ERR
		; Finish as soon as the whole announced body is in, without waiting for
		; the server to close. Required for keep-alive (no CLOSED arrives) and a
		; useful speedup otherwise.
		CALL	BODY_IS_COMPLETE
		JP	Z,.RX_DONE
		LD	A,(SAW_CLOSED)
		AND	A
		JP	Z,.LOOP
		CALL	SHOULD_FINISH_AFTER_CLOSED
		JR	Z,.RX_DONE
		JP	.LOOP

.PROCESS_ERR
	CALL	WIFI.UART_RX_RESUME
	LD	A,(HTTP_UNSUPPORTED_REASON)
	AND	A
	JP	NZ,HTTP_ERROR_EXIT
	JR	.ERR

.HTTP_ERR_POP
	POP	BC
	CALL	WIFI.UART_RX_RESUME
	JP	HTTP_ERROR_EXIT

.RX_DONE
	LD	A,(REDIRECT_PENDING)
	AND	A
	JR	NZ,.REDIRECT_DONE
	XOR	A
	LD	(LAST_TCP_ERR),A
.RX_ERROR_DONE
	LD	A,(HAVE_BODY)
	AND	A
	JR	Z,.NO_BODY
	CALL	FLUSH_HOLD
	JP	C,FILE_ERROR_EXIT
	JP	CHECK_CONTENT_LENGTH
.RX_ERROR
	LD	(LAST_TCP_ERR),A
	CP	RES_NOT_CONN
	JR	NZ,.RX_ERROR_NOT_CLOSED
	LD	A,1
	LD	(SAW_CLOSED),A
.RX_ERROR_NOT_CLOSED
		PRINT	WCOMMON.LINE_END
		CALL	PRINT_RECV_DEBUG
		CALL	PRINT_UART_OVERRUN_HINT
		LD	A,1
		LD	(DEBUG_PRINTED),A
		LD	A,(LAST_TCP_ERR)
		CP	RES_NOT_CONN
		JR	NZ,.RX_ERROR_DONE
		CALL	SHOULD_FINISH_AFTER_CLOSED
		JP	NZ,.LOOP
		JR	.RX_ERROR_DONE
.RX_UART_ERROR
	LD	A,RES_RS_TIMEOUT
	JR	.RX_ERROR
.NO_BODY
	LD	A,(GOT_RESPONSE)
	AND	A
	JR	Z,.NO_RESPONSE
	PRINTLN MSG_NO_BODY
	JR	.NO_BODY_ERR
.NO_RESPONSE
	PRINTLN MSG_NO_RESPONSE
.NO_BODY_ERR
	LD	A,(LAST_TCP_ERR)
	AND	A
	JR	NZ,.ERR
	LD	A,RES_RS_TIMEOUT
.ERR
	SCF
	RET
.REDIRECT_DONE
	XOR	A
	RET
.CANCELLED
	JP	CANCEL_EXIT

CHECK_HTTP_STATUS
	LD	A,B
	OR	C
	JR	Z,.ERR

.FIND_STATUS
	LD	A,B
	OR	A
	JR	NZ,.ENOUGH_BYTES
	LD	A,C
	CP	5
	JR	C,.ERR
.ENOUGH_BYTES
	CALL	MATCH_HTTP_PREFIX
	JR	Z,.FOUND
	INC	HL
	DEC	BC
	JR	.FIND_STATUS

.FOUND
	LD	(HTTP_STATUS_PTR),HL
	CALL	HTTP_STATUS_ACCEPTED
	RET	NC
.ERR
	SCF
	RET

MATCH_HTTP_PREFIX
	PUSH	HL,DE,BC
	LD	DE,HTTP_STATUS_PREFIX
	LD	B,5
.NEXT
	LD	A,(DE)
	CP	(HL)
	JR	NZ,.NO_MATCH
	INC	DE
	INC	HL
	DJNZ	.NEXT
	XOR	A
	POP	BC,DE,HL
	RET
.NO_MATCH
	LD	A,1
	OR	A
	POP	BC,DE,HL
	RET

HTTP_STATUS_ACCEPTED
	LD	B,16
.FIND_SPACE
	LD	A,(HL)
	CP	' '
	JR	Z,.SKIP_SPACES
	CP	13
	JR	Z,.ERR
	CP	10
	JR	Z,.ERR
	AND	A
	JR	Z,.ERR
	INC	HL
	DJNZ	.FIND_SPACE
	JR	.ERR
.SKIP_SPACES
	INC	HL
	LD	A,(HL)
	CP	' '
	JR	Z,.SKIP_SPACES
	LD	(HTTP_STATUS_CLASS),A
	LD	A,(RANGE_ACTIVE)
	AND	A
	JR	Z,.ACCEPT_CLASS
	LD	A,(HL)
	CP	'2'
	JR	NZ,.ERR
	INC	HL
	LD	A,(HL)
	CP	'0'
	JR	NZ,.ERR
	INC	HL
	LD	A,(HL)
	CP	'6'
	JR	NZ,.ERR
	XOR	A
	RET
.ACCEPT_CLASS
	LD	A,(HTTP_STATUS_CLASS)
	CP	'2'
	RET	Z
	CP	'3'
	RET	Z
.ERR
	SCF
	RET

ADJUST_TO_HTTP_STATUS
	LD	DE,(HTTP_STATUS_PTR)
	PUSH	DE
	LD	HL,RECV_BUFFER
	ADD	HL,BC
	POP	DE
	AND	A
	SBC	HL,DE
	LD	B,H
	LD	C,L
	EX	DE,HL
	RET

PROCESS_HTTP_DATA
	LD	A,(HEADER_DONE)
	AND	A
	JR	Z,.SCAN_HEADER
	CALL	CHECK_REDIRECT_HEADER
	RET	C
	LD	A,(REDIRECT_PENDING)
	AND	A
	RET	NZ
	CALL	CHECK_UNSUPPORTED_HTTP
	RET	C
	JP	WRITE_BODY

.SCAN_HEADER
	LD	A,B
	OR	C
	RET	Z
	LD	A,(HL)
	INC	HL
	DEC	BC
	PUSH	AF
	CALL	CAPTURE_HEADER_CHAR
	POP	AF
	CALL	UPDATE_HEADER_STATE
	LD	A,(HEADER_DONE)
	AND	A
	JR	Z,.SCAN_HEADER
	CALL	CHECK_REDIRECT_HEADER
	RET	C
	LD	A,(REDIRECT_PENDING)
	AND	A
	RET	NZ
	CALL	CHECK_UNSUPPORTED_HTTP
	RET	C
	JP	WRITE_BODY

CAPTURE_HEADER_CHAR
	PUSH	BC,DE,HL
	LD	E,A
	CP	13
	JR	Z,.DONE
	CP	10
	JR	Z,.LINE_DONE
	LD	A,(HEADER_LINE_LEN)
	CP	HEADER_LINE_SIZE-1
	JR	NC,.DONE
	LD	C,A
	LD	B,0
	LD	HL,HEADER_LINE
	ADD	HL,BC
	LD	(HL),E
	INC	A
	LD	(HEADER_LINE_LEN),A
	JR	.DONE
.LINE_DONE
	LD	A,(HEADER_LINE_LEN)
	LD	C,A
	LD	B,0
	LD	HL,HEADER_LINE
	ADD	HL,BC
	LD	(HL),0
	CALL	PARSE_HEADER_LINE
	XOR	A
	LD	(HEADER_LINE_LEN),A
.DONE
	POP	HL,DE,BC
	RET

PARSE_HEADER_LINE
	LD	HL,HEADER_LINE
	LD	DE,CONTENT_LENGTH_PREFIX
	CALL	MATCH_HEADER_PREFIX
	JR	NZ,.TRY_TRANSFER_ENCODING
.FOUND
	CALL	SKIP_HEADER_SPACES
	LD	A,(HL)
	CP	'0'
	RET	C
	CP	'9'+1
	RET	NC
	CALL	PARSE_CONTENT_LENGTH_U32
	LD	A,1
	LD	(CONTENT_LENGTH_SEEN),A
	RET

.TRY_TRANSFER_ENCODING
	LD	HL,HEADER_LINE
	LD	DE,TRANSFER_ENCODING_PREFIX
	CALL	MATCH_HEADER_PREFIX
	JR	NZ,.TRY_CONTENT_ENCODING
	CALL	SKIP_HEADER_SPACES
	LD	DE,CHUNKED_VALUE
	CALL	MATCH_TOKEN_CI
	RET	NZ
	LD	A,HTTP_UNSUPPORTED_CHUNKED
	LD	(HTTP_UNSUPPORTED_REASON),A
	RET

.TRY_CONTENT_ENCODING
	LD	HL,HEADER_LINE
	LD	DE,CONTENT_ENCODING_PREFIX
	CALL	MATCH_HEADER_PREFIX
	JR	NZ,.TRY_LOCATION
	CALL	SKIP_HEADER_SPACES
	LD	DE,GZIP_VALUE
	CALL	MATCH_TOKEN_CI
	RET	NZ
	LD	A,HTTP_UNSUPPORTED_GZIP
	LD	(HTTP_UNSUPPORTED_REASON),A
	RET

.TRY_LOCATION
	LD	HL,HEADER_LINE
	LD	DE,LOCATION_PREFIX
	CALL	MATCH_HEADER_PREFIX
	RET	NZ
.LOC_FOUND
	CALL	SKIP_HEADER_SPACES
	LD	DE,LOCATION_BUFF
	LD	C,URL_SIZE-1
	CALL	COPY_HEADER_VALUE
	LD	A,1
	LD	(LOCATION_SEEN),A
	RET

MATCH_HEADER_PREFIX
	LD	A,(DE)
	AND	A
	RET	Z
	CP	(HL)
	RET	NZ
	INC	DE
	INC	HL
	JR	MATCH_HEADER_PREFIX

MATCH_TOKEN_CI
	LD	A,(DE)
	AND	A
	RET	Z
	LD	C,A
	LD	A,(HL)
	CALL	ASCII_TO_LOWER
	CP	C
	RET	NZ
	INC	HL
	INC	DE
	JR	MATCH_TOKEN_CI

ASCII_TO_LOWER
	CP	'A'
	RET	C
	CP	'Z'+1
	RET	NC
	ADD	A,32
	RET

SKIP_HEADER_SPACES
	LD	A,(HL)
	CP	' '
	JR	Z,.NEXT
	CP	9
	RET	NZ
.NEXT
	INC	HL
	JR	SKIP_HEADER_SPACES

COPY_HEADER_VALUE
	LD	A,(HL)
	AND	A
	JR	Z,.DONE
	LD	A,C
	AND	A
	JR	Z,.DONE
	LD	A,(HL)
	LD	(DE),A
	INC	HL
	INC	DE
	DEC	C
	JR	COPY_HEADER_VALUE
.DONE
	XOR	A
	LD	(DE),A
	RET

CHECK_UNSUPPORTED_HTTP
	LD	A,(HTTP_UNSUPPORTED_REASON)
	AND	A
	RET	Z
	SCF
	RET

CHECK_REDIRECT_HEADER
	LD	A,(HTTP_STATUS_CLASS)
	CP	'3'
	JR	Z,.REDIRECT
	XOR	A
	RET
.REDIRECT
	LD	A,(LOCATION_SEEN)
	AND	A
	JR	Z,.ERR
	LD	A,1
	LD	(REDIRECT_PENDING),A
	XOR	A
	RET
.ERR
	LD	A,HTTP_UNSUPPORTED_REDIRECT
	LD	(HTTP_UNSUPPORTED_REASON),A
	LD	A,RES_ERROR
	SCF
	RET

FOLLOW_REDIRECT
	LD	A,(REDIRECT_COUNT)
	CP	REDIRECT_LIMIT
	JR	NC,.ERR
	INC	A
	LD	(REDIRECT_COUNT),A

	PRINT	MSG_REDIRECT
	PRINT	LOCATION_BUFF
	PRINT	WCOMMON.LINE_END

	LD	HL,LOCATION_BUFF
	LD	DE,HTTP_PREFIX
	CALL	UTIL.STARTSWITH
	JR	Z,.HTTP

	LD	HL,LOCATION_BUFF
	LD	DE,HTTPS_PREFIX
	CALL	UTIL.STARTSWITH
	JR	Z,.HTTPS

	LD	A,HTTP_UNSUPPORTED_REDIRECT
	LD	(HTTP_UNSUPPORTED_REASON),A
	JR	.ERR

.HTTP
	LD	HL,LOCATION_BUFF
	LD	DE,URL_BUFF
	CALL	COPY_ASCIIZ_DE
	CALL	PARSE_URL
	JR	C,.ERR
	CALL	SET_PORT_FLAG
	XOR	A
	RET

.HTTPS
	LD	A,HTTP_UNSUPPORTED_HTTPS
	LD	(HTTP_UNSUPPORTED_REASON),A
.ERR
	LD	A,RES_ERROR
	SCF
	RET

; ------------------------------------------------------
; Remove ESP-AT connection-close notification from the end of a receive block.
; Some ESP/jesperl streams append CLOSED after TCP payload bytes. It is not
; HTTP body and must never be written to the output file.
;
; Guard: only attempt the trim when we are past HTTP headers AND a
; Content-Length is known AND BODY_BYTES + BC strictly exceeds
; Content-Length. Without that, an HTTP body that legitimately ends with
; "CLOSED" plus CR/LF (text payload, log file, etc.) would be silently
; truncated. With the guard we only ever trim bytes that already overflow
; the announced body size.
; In/Out: BC - received byte count.
; ------------------------------------------------------
TRIM_CLOSED_SUFFIX
	LD	(TRIM_ORIG_LEN),BC
	LD	(TRIM_CAND_LEN),BC
	LD	A,(HEADER_DONE)
	AND	A
	JR	Z,.NO_MATCH
	LD	A,(CONTENT_LENGTH_SEEN)
	AND	A
	JR	Z,.NO_MATCH
	CALL	BODY_PLUS_BC_OVER_CL
	JR	NC,.NO_MATCH
	LD	HL,(RECV_DEST)
	ADD	HL,BC
.SKIP_TAIL_CRLF
	LD	BC,(TRIM_CAND_LEN)
	LD	A,B
	OR	C
	JR	Z,.NO_MATCH
	DEC	HL
	LD	A,(HL)
	CP	13
	JR	Z,.DROP_TAIL
	CP	10
	JR	Z,.DROP_TAIL
	INC	HL
	JR	.CHECK_CLOSED
.DROP_TAIL
	DEC	BC
	LD	(TRIM_CAND_LEN),BC
	JR	.SKIP_TAIL_CRLF
.CHECK_CLOSED
	LD	BC,(TRIM_CAND_LEN)
	LD	A,B
	AND	A
	JR	NZ,.HAVE_ENOUGH
	LD	A,C
	CP	6
	JR	C,.NO_MATCH
.HAVE_ENOUGH
	PUSH	HL
	LD	DE,6
	AND	A
	SBC	HL,DE
	LD	DE,CLOSED_WORD
	LD	B,6
.MATCH
	LD	A,(DE)
	CP	(HL)
	JR	NZ,.RESTORE_NO_MATCH
	INC	DE
	INC	HL
	DJNZ	.MATCH
	POP	HL
	LD	HL,(TRIM_CAND_LEN)
	LD	DE,6
	AND	A
	SBC	HL,DE
	LD	B,H
	LD	C,L
	LD	A,1
	LD	(SAW_CLOSED),A
	RET
.RESTORE_NO_MATCH
	POP	HL
.NO_MATCH
	LD	BC,(TRIM_ORIG_LEN)
	RET

; ------------------------------------------------------
; CF=1 iff (BODY_BYTES + BC) > CONTENT_LENGTH (32-bit unsigned, strict).
; Preserves BC. Trashes A, DE, HL.
; ------------------------------------------------------
BODY_PLUS_BC_OVER_CL
	PUSH	BC
	LD	HL,(BODY_BYTES)
	ADD	HL,BC
	LD	(TRIM_TMP),HL
	LD	HL,(BODY_BYTES+2)
	JR	NC,.NO_CARRY
	INC	HL
.NO_CARRY
	LD	(TRIM_TMP+2),HL

	; Compare 32-bit TRIM_TMP > CONTENT_LENGTH; high words first.
	LD	HL,(CONTENT_LENGTH+2)
	LD	DE,(TRIM_TMP+2)
	OR	A
	SBC	HL,DE
	JR	C,.OVER				; CL_hi < TMP_hi -> over
	JR	NZ,.NOT_OVER			; CL_hi > TMP_hi -> not over
	; equal high words; compare low
	LD	HL,(CONTENT_LENGTH)
	LD	DE,(TRIM_TMP)
	OR	A
	SBC	HL,DE
	JR	C,.OVER				; CL_lo < TMP_lo -> over
	; CL_lo >= TMP_lo (including equal) -> not strictly over
.NOT_OVER
	POP	BC
	AND	A
	RET
.OVER
	POP	BC
	SCF
	RET

UPDATE_HEADER_STATE
	LD	E,A
	LD	A,(HDR_STATE)
	AND	A
	JR	Z,.STATE0
	CP	1
	JR	Z,.STATE1
	CP	2
	JR	Z,.STATE2

; State 3: CR LF CR seen, waiting for LF.
.STATE3
	LD	A,E
	CP	10
	JR	Z,.DONE
	CP	13
	JR	Z,.SET3
	XOR	A
	JR	.STORE
.STATE0
	LD	A,E
	CP	13
	JR	Z,.SET1
	XOR	A
	JR	.STORE
.STATE1
	LD	A,E
	CP	10
	JR	Z,.SET2
	CP	13
	JR	Z,.SET1
	XOR	A
	JR	.STORE
.STATE2
	LD	A,E
	CP	13
	JR	Z,.SET3
	XOR	A
	JR	.STORE
.SET1
	LD	A,1
	JR	.STORE
.SET2
	LD	A,2
	JR	.STORE
.SET3
	LD	A,3
	JR	.STORE
.DONE
	LD	A,1
	LD	(HEADER_DONE),A
	RET
.STORE
	LD	(HDR_STATE),A
	RET

WRITE_BODY
	; RX is already paused by RECEIVE_HTTP main loop; resume happens at .LOOP top.
	LD	A,B
	OR	C
	RET	Z
	LD	A,(BODY_LOG_PRINTED)
	AND	A
	JR	NZ,.LOG_DONE
	PUSH	HL
	PUSH	BC
	CALL	Z,PRINT_BODY_LOG_HEADER
	POP	BC
	POP	HL
.LOG_DONE
	LD	A,(HOLD_MODE)
	AND	A
	JR	NZ,.HOLD
	; Safe zone: still more than HOLD_TAIL_MARGIN bytes to come (and reads are
	; capped to stop exactly at that boundary), so a DSS_WRITE pause here can
	; never coincide with the connection close.
	PUSH	BC
	CALL	WRITE_OUTPUT
	POP	BC
	RET	C
	CALL	ADD_BODY_BYTES
	LD	HL,BODY_BYTES
	LD	DE,CONTENT_LENGTH
	CALL	TPUT.PROGRESS
	LD	A,1
	LD	(HAVE_BODY),A
	RET
.HOLD
	; Retain-tail: do NOT write to disk. Hold mode is entered at the loop top
	; before the read, so RECEIVE already placed these bytes contiguously at
	; RECV_BUFFER+HOLD_LEN (HL == RECV_DEST). They are flushed once the
	; transfer completes (FLUSH_HOLD). With no write pause RTS stays asserted,
	; so ESP delivers every +IPD before CLOSED and drops nothing.
	LD	HL,(HOLD_LEN)
	ADD	HL,BC
	LD	(HOLD_LEN),HL
	CALL	ADD_BODY_BYTES
	LD	HL,BODY_BYTES
	LD	DE,CONTENT_LENGTH
	CALL	TPUT.PROGRESS
	LD	A,1
	LD	(HAVE_BODY),A
	RET

; ------------------------------------------------------
; Enter retain-tail (hold) mode once the announced body has at most
; HOLD_TAIL_MARGIN bytes left to receive. Called at the loop top BEFORE the
; read so the final bytes are accumulated, never written under an RTS-off
; pause. Latches HOLD_MODE; never clears it mid-transfer. No-op without a
; known Content-Length.
; ------------------------------------------------------
UPDATE_HOLD_MODE
	LD	A,(HOLD_MODE)
	AND	A
	RET	NZ
	LD	A,(CONTENT_LENGTH_SEEN)
	AND	A
	RET	Z
	CALL	BODY_REMAINING			; HL=low, A=0 and Z if <65536 & not negative
	RET	NZ				; remaining >= 65536 or CL < BODY -> not yet
	LD	A,H
	OR	L
	RET	Z				; remaining 0 -> done, no need to hold
	EX	DE,HL				; DE = remaining
	LD	HL,HOLD_TAIL_MARGIN
	OR	A
	SBC	HL,DE				; MARGIN - remaining; CF=1 -> MARGIN < remaining
	RET	C				; remaining > MARGIN -> not yet
	LD	A,1
	LD	(HOLD_MODE),A
	LD	HL,0
	LD	(HOLD_LEN),HL
	RET

; ------------------------------------------------------
; remaining = CONTENT_LENGTH - BODY_BYTES (32-bit).
; Out: if the high word is 0 and CL>=BODY: Z=1, A=0, HL=remaining low word.
;      otherwise NZ (remaining >= 65536, or CL < BODY). Trashes BC,DE.
; ------------------------------------------------------
BODY_REMAINING
	OR	A
	LD	HL,(CONTENT_LENGTH)
	LD	DE,(BODY_BYTES)
	SBC	HL,DE
	PUSH	HL				; remaining low
	LD	HL,(CONTENT_LENGTH+2)
	LD	DE,(BODY_BYTES+2)
	SBC	HL,DE				; remaining high, CF=borrow if CL<BODY
	JR	C,.NEG
	LD	A,H
	OR	L				; Z iff high word 0
	POP	HL				; remaining low (preserves flags)
	RET
.NEG
	POP	HL
	OR	1				; force NZ
	RET

; ------------------------------------------------------
; Compute RECV_DEST / RECV_CAP for the next TCP.RECEIVE. In hold mode data is
; appended at RECV_BUFFER+HOLD_LEN. In stream mode it overwrites from the start,
; but the capacity is clamped so a read never crosses into the final
; HOLD_TAIL_MARGIN bytes (that tail is always taken in hold mode instead).
; ------------------------------------------------------
SETUP_RECV_DEST
	LD	A,(HOLD_MODE)
	AND	A
	JR	Z,.STREAM
	; hold: dest = RECV_BUFFER + HOLD_LEN, cap = RECV_BUFFER_SIZE - HOLD_LEN
	LD	HL,RECV_BUFFER
	LD	DE,(HOLD_LEN)
	ADD	HL,DE
	LD	(RECV_DEST),HL
	EX	DE,HL
	LD	HL,RECV_BUFFER+RECV_BUFFER_SIZE
	OR	A
	SBC	HL,DE
	LD	(RECV_CAP),HL
	RET
.STREAM
	LD	HL,RECV_BUFFER
	LD	(RECV_DEST),HL
	LD	HL,RECV_BUFFER_SIZE
	LD	(RECV_CAP),HL
	LD	A,(CONTENT_LENGTH_SEEN)
	AND	A
	RET	Z
	CALL	BODY_REMAINING
	RET	NZ				; remaining >= 65536 -> full buffer
	; allowed = remaining - HOLD_TAIL_MARGIN; if <= 0 hold mode handles it
	LD	DE,HOLD_TAIL_MARGIN
	OR	A
	SBC	HL,DE
	RET	C				; remaining < MARGIN -> next loop enters hold
	RET	Z				; remaining == MARGIN -> next loop enters hold
	; cap = min(RECV_BUFFER_SIZE, allowed) so the last stream read ends exactly
	; HOLD_TAIL_MARGIN before the body end.
	LD	DE,RECV_BUFFER_SIZE
	PUSH	HL
	OR	A
	SBC	HL,DE
	POP	HL				; HL = allowed
	RET	NC				; allowed >= buffer -> keep full buffer cap
	LD	(RECV_CAP),HL
	RET

; ------------------------------------------------------
; Write any retained tail (HOLD_LEN bytes from RECV_BUFFER) to the output file
; and leave hold mode. Called once the transfer body is complete. CF=1 on a
; DSS_WRITE error.
; ------------------------------------------------------
FLUSH_HOLD
	LD	A,(HOLD_MODE)
	AND	A
	RET	Z
	LD	BC,(HOLD_LEN)
	LD	A,B
	OR	C
	JR	Z,.CLEAR
	LD	HL,RECV_BUFFER
	CALL	WRITE_OUTPUT
	RET	C
.CLEAR
	XOR	A
	LD	(HOLD_MODE),A
	LD	HL,0
	LD	(HOLD_LEN),HL
	XOR	A
	RET

PRINT_BODY_LOG_HEADER
