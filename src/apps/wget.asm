	LD	HL,U32_TEMP

U32_SHL1_AT_HL
	SLA	(HL)
	INC	HL
	RL	(HL)
	INC	HL
	RL	(HL)
	INC	HL
	RL	(HL)
	RET

U32_ADD_TEMP_TO_CONTENT
	LD	HL,CONTENT_LENGTH
	LD	DE,U32_TEMP
	LD	A,(DE)
	ADD	A,(HL)
	LD	(HL),A
	INC	HL
	INC	DE
	LD	A,(DE)
	ADC	A,(HL)
	LD	(HL),A
	INC	HL
	INC	DE
	LD	A,(DE)
	ADC	A,(HL)
	LD	(HL),A
	INC	HL
	INC	DE
	LD	A,(DE)
	ADC	A,(HL)
	LD	(HL),A
	RET

U32_ADD_A_CONTENT
