	LD	D,0
	LD	HL,CONTENT_LENGTH
	LD	A,(HL)
	ADD	A,E
	LD	(HL),A
	INC	HL
	LD	A,(HL)
	ADC	A,D
	LD	(HL),A
	INC	HL
	LD	A,(HL)
	ADC	A,D
	LD	(HL),A
	INC	HL
	LD	A,(HL)
	ADC	A,D
	LD	(HL),A
	RET

; ------------------------------------------------------
; Print little-endian 32-bit unsigned integer at HL as decimal.
; ------------------------------------------------------
PRINT_U32_AT_HL
	LD	DE,U32_PRINT_VALUE
	LD	BC,4
.COPY
	LD	A,(HL)
	LD	(DE),A
	INC	HL
	INC	DE
	DEC	BC
	LD	A,B
	OR	C
	JR	NZ,.COPY
	XOR	A
	LD	(U32_PRINT_STARTED),A
	LD	IX,U32_POW10_TABLE
	LD	B,10
.POW_LOOP
	XOR	A
	LD	(U32_PRINT_DIGIT),A
.SUB_LOOP
	PUSH	BC,IX
	CALL	U32_COMPARE_PRINT_IX
	POP	IX,BC
	JR	C,.DIGIT_READY
	PUSH	BC,IX
	CALL	U32_SUB_PRINT_IX
	POP	IX,BC
	LD	A,(U32_PRINT_DIGIT)
	INC	A
	LD	(U32_PRINT_DIGIT),A
	JR	.SUB_LOOP
.DIGIT_READY
	LD	A,(U32_PRINT_DIGIT)
	AND	A
	JR	NZ,.PRINT_DIGIT
	LD	A,(U32_PRINT_STARTED)
	AND	A
	JR	Z,.NEXT_POWER
	XOR	A
.PRINT_DIGIT
	ADD	A,'0'
	CALL	PUT_CHAR
	LD	A,1
	LD	(U32_PRINT_STARTED),A
.NEXT_POWER
	LD	DE,4
	ADD	IX,DE
	DJNZ	.POW_LOOP
	LD	A,(U32_PRINT_STARTED)
	AND	A
	RET	NZ
	LD	A,'0'
	JP	PUT_CHAR

; CF=0 when value >= power, CF=1 when value < power.
U32_COMPARE_PRINT_IX
	LD	HL,U32_PRINT_VALUE+3
	LD	A,(IX+3)
	CP	(HL)
	JR	NZ,.NE
	DEC	HL
	LD	A,(IX+2)
	CP	(HL)
	JR	NZ,.NE
	DEC	HL
	LD	A,(IX+1)
	CP	(HL)
	JR	NZ,.NE
	DEC	HL
	LD	A,(IX+0)
	CP	(HL)
	JR	NZ,.NE
	AND	A
	RET
.NE
	JR	C,.VALUE_GREATER
	SCF
	RET
.VALUE_GREATER
	AND	A
	RET

U32_SUB_PRINT_IX
	LD	HL,U32_PRINT_VALUE
	LD	A,(HL)
	SUB	(IX+0)
	LD	(HL),A
	INC	HL
	LD	A,(HL)
	SBC	A,(IX+1)
	LD	(HL),A
	INC	HL
	LD	A,(HL)
	SBC	A,(IX+2)
	LD	(HL),A
	INC	HL
	LD	A,(HL)
	SBC	A,(IX+3)
	LD	(HL),A
	RET

