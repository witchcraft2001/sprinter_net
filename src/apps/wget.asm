	ADD	HL,BC
	LD	(TOTAL_BYTES),HL
	RET	NC
	LD	HL,(TOTAL_BYTES+2)
	INC	HL
	LD	(TOTAL_BYTES+2),HL
	RET

CHECK_CONTENT_LENGTH
	LD	A,(CONTENT_LENGTH_SEEN)
	AND	A
	JR	Z,CHECK_CL_OK
	CALL	CMP_BODY_TO_CONTENT
	JR	Z,CHECK_CL_OK
	JR	C,CHECK_CL_INCOMPLETE
	JR	PRINT_CONTENT_LENGTH_MISMATCH
PRINT_CONTENT_LENGTH_MISMATCH
	PRINT	WCOMMON.LINE_END
	PRINTLN MSG_INCOMPLETE
	PRINT	MSG_EXPECTED
	LD	HL,CONTENT_LENGTH
	CALL	PRINT_U32_AT_HL
	PRINTLN	MSG_BYTES
	CALL	PRINT_DOWNLOADED_BYTES
	LD	A,(DEBUG_PRINTED)
	AND	A
	JR	NZ,.DEBUG_DONE
	CALL	PRINT_RECV_DEBUG
.DEBUG_DONE
	CALL	PRINT_UART_OVERRUN_HINT
	LD	A,RES_RS_TIMEOUT
	SCF
	RET
CHECK_CL_INCOMPLETE
	LD	A,1
	LD	(HTTP_INCOMPLETE),A
	LD	A,RES_RS_TIMEOUT
	SCF
	RET
CHECK_CL_OK
	XOR	A
	RET

; Compare BODY_BYTES to CONTENT_LENGTH as unsigned 32-bit values.
; Out: Z=1 if equal; CF=1 if BODY_BYTES < CONTENT_LENGTH.
CMP_BODY_TO_CONTENT
	LD	HL,CONTENT_LENGTH+3
	LD	DE,BODY_BYTES+3
	LD	B,4
.CMP
	LD	A,(DE)
	CP	(HL)
	RET	NZ
	DEC	DE
	DEC	HL
	DJNZ	.CMP
	XOR	A
	RET

PRINT_UART_OVERRUN_HINT
	LD	A,(TCP.LSR_ACCUM)
	AND	LSR_OE
	RET	Z
	PRINTLN	MSG_UART_OVERRUN
	RET

; Z=1 iff Content-Length is known and BODY_BYTES >= CONTENT_LENGTH.
BODY_IS_COMPLETE
	LD	A,(CONTENT_LENGTH_SEEN)
	AND	A
	JR	Z,.INCOMPLETE
	OR	A
	LD	HL,(CONTENT_LENGTH)
	LD	DE,(BODY_BYTES)
	SBC	HL,DE
	LD	B,H
	LD	C,L			; BC = low(CL - BODY)
	LD	HL,(CONTENT_LENGTH+2)
	LD	DE,(BODY_BYTES+2)
	SBC	HL,DE			; HL = high(CL - BODY); CF=1 if CL < BODY
	JR	C,.COMPLETE		; body overshoots CL -> complete
	LD	A,H
	OR	L
	OR	B
	OR	C
	JR	Z,.COMPLETE		; remaining == 0 -> complete
.INCOMPLETE
	OR	0xFF
	RET
.COMPLETE
	XOR	A
	RET

SHOULD_FINISH_AFTER_CLOSED
	LD	A,(CONTENT_LENGTH_SEEN)
	AND	A
	RET	Z
	LD	HL,CONTENT_LENGTH
	LD	DE,BODY_BYTES
	LD	B,4
.CMP
	LD	A,(DE)
	CP	(HL)
	JR	NZ,.NOT_YET
	INC	DE
	INC	HL
	DJNZ	.CMP
	XOR	A
	RET
.NOT_YET
	LD	A,1
	OR	A
	RET

UART_RX_ERROR_SEEN
	LD	A,(TCP.LSR_ACCUM)
	AND	LSR_OE | LSR_PE | LSR_FE | LSR_BI | LSR_RCVE
	RET

