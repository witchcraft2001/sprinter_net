; ======================================================
; WGET for Sprinter DSS Network Kit
; Minimal HTTP/1.0 downloader over ESP-AT TCP.
; ======================================================

EXE_VERSION		EQU 1
DEFAULT_TIMEOUT		EQU 2000
RECV_TIMEOUT		EQU 7000
RECV_BUFFER_SIZE	EQU 1500
URL_SIZE		EQU 160
HOST_SIZE		EQU 96
PORT_SIZE		EQU 8
PATH_SIZE		EQU 128
FILE_SIZE		EQU 80
REQ_SIZE		EQU 320
HEADER_LINE_SIZE	EQU 80
DSS_CREATE_OVERWRITE	EQU 0x0A
NO_HANDLE		EQU 0xFF

	DEVICE NOSLOT64K

	INCLUDE "macro.inc"
	INCLUDE "dss.inc"

	MODULE MAIN

	ORG 0x8080

EXE_HEADER
	DB "EXE"
	DB EXE_VERSION
	DW 0x0080
	DW 0
	DW 0
	DW 0
	DW 0
	DW 0
	DW START
	DW START
	DW STACK_TOP
	DS 106, 0

	ORG 0x8100
@STACK_TOP

START
	LD	A,NO_HANDLE
	LD	(OUT_FH),A
	XOR	A
	LD	(TCP_IS_OPEN),A

	CALL	ISA.ISA_RESET
	CALL	WCOMMON.INIT_VMODE
	PRINTLN MSG_START

	CALL	PARSE_CMD_LINE
	JP	C,USAGE
	CALL	PARSE_URL
	JP	C,USAGE
	CALL	SET_PORT_FLAG

	CALL	OPEN_OUTPUT_FILE
	JP	C,FILE_ERROR_EXIT

	CALL	WIFI.UART_FIND
	JP	C,NO_WIFI

	CALL	NETCFG.LOAD
	CALL	NETCFG.APPLY_UART_BAUD
	CALL	WIFI.UART_INIT
	PRINTLN MSG_UART_READY

	LD	HL,CMD_AT
	CALL	SEND_CMD_RECOVER

	LD	HL,CMD_ECHO_OFF
	CALL	SEND_CMD

	LD	HL,CMD_CIPMUX_0
	CALL	SEND_CMD

	PRINT MSG_CONNECTING
	PRINT HOST_BUFF
	LD	A,(PORT_IS_DEFAULT)
	AND	A
	JR	NZ,.CONNECT_DEFAULT_PORT
	PRINT MSG_COLON
	PRINT PORT_BUFF
.CONNECT_DEFAULT_PORT
	PRINT WCOMMON.LINE_END
	LD	HL,HOST_BUFF
	LD	DE,PORT_BUFF
	CALL	TCP.OPEN
	JP	C,TCP_ERROR_EXIT
	LD	A,1
	LD	(TCP_IS_OPEN),A

	CALL	BUILD_HTTP_REQUEST
	PRINTLN MSG_DOWNLOADING
	LD	HL,REQ_BUFF
	CALL	UTIL.STRLEN
	CALL	TCP.SEND_BUFFER
	JP	C,TCP_ERROR_EXIT

	CALL	RECEIVE_HTTP
	JP	C,TCP_ERROR_EXIT

	CALL	TCP.CLOSE
	XOR	A
	LD	(TCP_IS_OPEN),A
	CALL	CLOSE_OUTPUT_FILE
	JP	C,FILE_ERROR_EXIT

	PRINT WCOMMON.LINE_END
	PRINTLN MSG_DONE
	LD	B,0
	JP	WCOMMON.EXIT

NO_WIFI
	CALL	CLOSE_OUTPUT_FILE
	PRINTLN MSG_WIFI_NOT_FOUND
	LD	B,2
	JP	WCOMMON.EXIT

USAGE
	PRINTLN MSG_USAGE
	LD	B,1
	JP	WCOMMON.EXIT

TCP_ERROR_EXIT
	PUSH	AF
	LD	A,(TCP_IS_OPEN)
	AND	A
	CALL	NZ,TCP.CLOSE
	CALL	CLOSE_OUTPUT_FILE
	POP	AF
	ADD	A,'0'
	LD	(MSG_ERROR_NO),A
	PRINTLN MSG_NET_ERROR
	LD	B,3
	JP	WCOMMON.EXIT

FILE_ERROR_EXIT
	CALL	PRINT_FILE_ERROR
	LD	B,5
	JP	WCOMMON.EXIT

HTTP_ERROR_EXIT
	LD	A,(TCP_IS_OPEN)
	AND	A
	CALL	NZ,TCP.CLOSE
	CALL	CLOSE_OUTPUT_FILE
	CALL	PRINT_HTTP_STATUS
	PRINTLN MSG_HTTP_UNSUPPORTED
	LD	B,3
	JP	WCOMMON.EXIT

; ------------------------------------------------------
; Parse command line: WGET.EXE http://host[:port]/path FILE
; ------------------------------------------------------
PARSE_CMD_LINE
	LD	HL,0x8080
	LD	A,(HL)
	AND	A
	JR	Z,.ERR
	LD	B,A
	INC	HL
	CALL	SKIP_SPACES
	JR	C,.ERR
	LD	DE,URL_BUFF
	LD	C,URL_SIZE-1
	CALL	COPY_ARG
	JR	C,.ERR
	CALL	SKIP_SPACES
	JR	C,.ERR
	LD	DE,OUT_FILE
	LD	C,FILE_SIZE-1
	CALL	COPY_ARG
	RET	NC
.ERR
	SCF
	RET

SKIP_SPACES
	LD	A,B
	AND	A
	JR	Z,.ERR
	LD	A,(HL)
	CP	0x21
	RET	NC
	INC	HL
	DJNZ	SKIP_SPACES
.ERR
	SCF
	RET

COPY_ARG
	XOR	A
	LD	(ARG_LEN),A
.NEXT
	LD	A,B
	AND	A
	JR	Z,.END
	LD	A,(HL)
	CP	0x21
	JR	C,.END
	LD	A,C
	AND	A
	JR	Z,.ERR
	LD	A,(HL)
	LD	(DE),A
	INC	DE
	INC	HL
	DEC	B
	DEC	C
	LD	A,(ARG_LEN)
	INC	A
	LD	(ARG_LEN),A
	JR	.NEXT
.END
	XOR	A
	LD	(DE),A
	LD	A,(ARG_LEN)
	AND	A
	RET	NZ
.ERR
	SCF
	RET

; ------------------------------------------------------
; Parse URL_BUFF into HOST_BUFF, PORT_BUFF and PATH_BUFF.
; ------------------------------------------------------
PARSE_URL
	LD	HL,URL_BUFF
	LD	DE,HTTP_PREFIX
	CALL	UTIL.STARTSWITH
	JP	NZ,.ERR
	LD	BC,7
	ADD	HL,BC

	LD	DE,HOST_BUFF
	LD	C,HOST_SIZE-1
.HOST
	LD	A,(HL)
	AND	A
	JR	Z,.HOST_DONE
	CP	'/'
	JR	Z,.HOST_DONE
	CP	':'
	JR	Z,.PORT_START
	LD	A,C
	AND	A
	JR	Z,.ERR
	LD	A,(HL)
	LD	(DE),A
	INC	DE
	INC	HL
	DEC	C
	JR	.HOST

.HOST_DONE
	XOR	A
	LD	(DE),A
	LD	A,(HOST_BUFF)
	AND	A
	JR	Z,.ERR
	PUSH	HL
	LD	DE,DEFAULT_PORT
	CALL	COPY_PORT_DEFAULT
	POP	HL
	JR	.PATH_START

.PORT_START
	XOR	A
	LD	(DE),A
	INC	HL
	LD	DE,PORT_BUFF
	LD	C,PORT_SIZE-1
.PORT
	LD	A,(HL)
	AND	A
	JR	Z,.PORT_DONE
	CP	'/'
	JR	Z,.PORT_DONE
	CP	'0'
	JR	C,.ERR
	CP	'9'+1
	JR	NC,.ERR
	LD	A,C
	AND	A
	JR	Z,.ERR
	LD	A,(HL)
	LD	(DE),A
	INC	DE
	INC	HL
	DEC	C
	JR	.PORT
.PORT_DONE
	XOR	A
	LD	(DE),A
	LD	A,(PORT_BUFF)
	AND	A
	JR	Z,.ERR

.PATH_START
	LD	A,(HL)
	CP	'/'
	JR	Z,.COPY_PATH
	LD	HL,DEFAULT_PATH
	LD	DE,PATH_BUFF
	JP	COPY_ASCIIZ_DE

.COPY_PATH
	LD	DE,PATH_BUFF
	LD	C,PATH_SIZE-1
.PATH
	LD	A,(HL)
	AND	A
	JR	Z,.PATH_DONE
	LD	A,C
	AND	A
	JR	Z,.ERR
	LD	A,(HL)
	LD	(DE),A
	INC	DE
	INC	HL
	DEC	C
	JR	.PATH
.PATH_DONE
	XOR	A
	LD	(DE),A
	RET
.ERR
	SCF
	RET

COPY_PORT_DEFAULT
	LD	HL,DEFAULT_PORT
	LD	DE,PORT_BUFF
	JP	COPY_ASCIIZ_DE

COPY_ASCIIZ_DE
	LD	A,(HL)
	LD	(DE),A
	AND	A
	RET	Z
	INC	HL
	INC	DE
	JR	COPY_ASCIIZ_DE

; ------------------------------------------------------
; Local output file helpers.
; ------------------------------------------------------
OPEN_OUTPUT_FILE
	PRINT MSG_OUTPUT
	PRINT OUT_FILE
	PRINT WCOMMON.LINE_END
	LD	HL,OUT_FILE
	LD	C,DSS_CREATE_OVERWRITE
	RST	DSS
	RET	C
	LD	(OUT_FH),A
	RET

CLOSE_OUTPUT_FILE
	LD	A,(OUT_FH)
	CP	NO_HANDLE
	RET	Z
	LD	C,DSS_CLOSE_FILE
	RST	DSS
	LD	A,NO_HANDLE
	LD	(OUT_FH),A
	RET

WRITE_OUTPUT
	LD	A,B
	OR	C
	RET	Z
	LD	A,(OUT_FH)
	LD	D,B
	LD	E,C
	LD	C,DSS_WRITE
	RST	DSS
	RET

PRINT_FILE_ERROR
	PRINT	MSG_FILE_ERROR
	CALL	DSS_ERROR.GET_ERR_MSG
	PRINTLN_HL
	RET

; ------------------------------------------------------
; ESP/TCP command helpers.
; ------------------------------------------------------
SEND_CMD
	LD	DE,WIFI.RS_BUFF
	LD	BC,DEFAULT_TIMEOUT
	CALL	WIFI.UART_TX_CMD
	AND	A
	RET	Z
	JP	TCP_ERROR_EXIT

SEND_CMD_RECOVER
	PUSH	HL
	LD	DE,WIFI.RS_BUFF
	LD	BC,DEFAULT_TIMEOUT
	CALL	WIFI.UART_TX_CMD
	AND	A
	JR	Z,.OK

	PRINTLN MSG_RESETTING_ESP
	CALL	WIFI.ESP_RESET
	CALL	WIFI.UART_SET_DEFAULT_DIVISOR
	CALL	WIFI.UART_INIT
	POP	HL
	JP	SEND_CMD
.OK
	POP	HL
	RET

; ------------------------------------------------------
; Build HTTP/1.0 GET request in REQ_BUFF.
; ------------------------------------------------------
BUILD_HTTP_REQUEST
	CALL	SET_PORT_FLAG
	LD	HL,REQ_BUFF
	LD	DE,REQ_GET
	CALL	APPEND_STR
	LD	IX,PATH_BUFF
	CALL	APPEND_IX_STR
	LD	DE,REQ_HTTP
	CALL	APPEND_STR
	LD	IX,HOST_BUFF
	CALL	APPEND_IX_STR
	LD	A,(PORT_IS_DEFAULT)
	AND	A
	JR	NZ,.NO_HOST_PORT
	LD	DE,REQ_PORT_SEP
	CALL	APPEND_STR
	LD	IX,PORT_BUFF
	CALL	APPEND_IX_STR
.NO_HOST_PORT
	LD	DE,REQ_END
	JP	APPEND_STR

; ------------------------------------------------------
; Set PORT_IS_DEFAULT=1 when port is "80", otherwise 0.
; ------------------------------------------------------
SET_PORT_FLAG
	LD	HL,DEFAULT_PORT
	LD	DE,PORT_BUFF
	CALL	UTIL.STRCMP
	JR	C,.CUSTOM
	LD	A,1
	LD	(PORT_IS_DEFAULT),A
	RET
.CUSTOM
	XOR	A
	LD	(PORT_IS_DEFAULT),A
	RET

; ------------------------------------------------------
; Receive HTTP response, skip headers and write body to file.
; ------------------------------------------------------
RECEIVE_HTTP
	XOR	A
	LD	(GOT_RESPONSE),A
	LD	(HEADER_DONE),A
	LD	(HDR_STATE),A
	LD	(HAVE_BODY),A
	LD	(HTTP_STATUS_PTR),A
	LD	(HTTP_STATUS_PTR+1),A
	LD	(ADJUST_RESPONSE),A
	LD	(CONTENT_LENGTH),A
	LD	(CONTENT_LENGTH+1),A
	LD	(CONTENT_LENGTH+2),A
	LD	(CONTENT_LENGTH+3),A
	LD	(CONTENT_LENGTH_SEEN),A
	LD	(BODY_BYTES),A
	LD	(BODY_BYTES+1),A
	LD	(BODY_BYTES+2),A
	LD	(BODY_BYTES+3),A
	LD	(HEADER_LINE_LEN),A

.LOOP
	LD	HL,RECV_BUFFER
	LD	BC,RECV_BUFFER_SIZE
	LD	DE,RECV_TIMEOUT
	CALL	TCP.RECEIVE
	JR	C,.RX_ERROR
	LD	A,B
	OR	C
	JR	Z,.RX_DONE

	PUSH	BC
	LD	A,(GOT_RESPONSE)
	AND	A
	JR	NZ,.STATUS_DONE
	LD	A,1
	LD	(GOT_RESPONSE),A
	LD	HL,RECV_BUFFER
	CALL	CHECK_HTTP_STATUS
	JR	C,.HTTP_ERR_POP
	LD	A,1
	LD	(ADJUST_RESPONSE),A
.STATUS_DONE
	POP	BC
	LD	A,(ADJUST_RESPONSE)
	AND	A
	JR	Z,.PROCESS_FROM_START
	XOR	A
	LD	(ADJUST_RESPONSE),A
	CALL	ADJUST_TO_HTTP_STATUS
	JR	.PROCESS_DATA
.PROCESS_FROM_START
	LD	HL,RECV_BUFFER
.PROCESS_DATA
	CALL	PROCESS_HTTP_DATA
	JR	C,.ERR
	JR	.LOOP

.HTTP_ERR_POP
	POP	BC
	JP	HTTP_ERROR_EXIT

.RX_DONE
	XOR	A
	LD	(LAST_TCP_ERR),A
.RX_ERROR_DONE
	LD	A,(HAVE_BODY)
	AND	A
	JR	Z,.NO_BODY
	JP	CHECK_CONTENT_LENGTH
.RX_ERROR
	LD	(LAST_TCP_ERR),A
	JR	.RX_ERROR_DONE
.NO_BODY
	LD	A,(GOT_RESPONSE)
	AND	A
	JR	Z,.NO_RESPONSE
	PRINTLN MSG_NO_BODY
	JR	.NO_BODY_ERR
.NO_RESPONSE
	PRINTLN MSG_NO_RESPONSE
.NO_BODY_ERR
	LD	A,(LAST_TCP_ERR)
	AND	A
	JR	NZ,.ERR
	LD	A,RES_RS_TIMEOUT
.ERR
	SCF
	RET

CHECK_HTTP_STATUS
	LD	A,B
	OR	C
	JR	Z,.ERR

.FIND_STATUS
	LD	A,B
	OR	A
	JR	NZ,.ENOUGH_BYTES
	LD	A,C
	CP	5
	JR	C,.ERR
.ENOUGH_BYTES
	CALL	MATCH_HTTP_PREFIX
	JR	Z,.FOUND
	INC	HL
	DEC	BC
	JR	.FIND_STATUS

.FOUND
	LD	(HTTP_STATUS_PTR),HL
	CALL	HTTP_STATUS_IS_2XX
	RET	NC
.ERR
	SCF
	RET

MATCH_HTTP_PREFIX
	PUSH	HL,DE,BC
	LD	DE,HTTP_STATUS_PREFIX
	LD	B,5
.NEXT
	LD	A,(DE)
	CP	(HL)
	JR	NZ,.NO_MATCH
	INC	DE
	INC	HL
	DJNZ	.NEXT
	XOR	A
	POP	BC,DE,HL
	RET
.NO_MATCH
	LD	A,1
	OR	A
	POP	BC,DE,HL
	RET

HTTP_STATUS_IS_2XX
	LD	B,16
.FIND_SPACE
	LD	A,(HL)
	CP	' '
	JR	Z,.SKIP_SPACES
	CP	13
	JR	Z,.ERR
	CP	10
	JR	Z,.ERR
	AND	A
	JR	Z,.ERR
	INC	HL
	DJNZ	.FIND_SPACE
	JR	.ERR
.SKIP_SPACES
	INC	HL
	LD	A,(HL)
	CP	' '
	JR	Z,.SKIP_SPACES
	CP	'2'
	RET	Z
.ERR
	SCF
	RET

ADJUST_TO_HTTP_STATUS
	LD	DE,(HTTP_STATUS_PTR)
	PUSH	DE
	LD	HL,RECV_BUFFER
	ADD	HL,BC
	POP	DE
	AND	A
	SBC	HL,DE
	LD	B,H
	LD	C,L
	EX	DE,HL
	RET

PROCESS_HTTP_DATA
	LD	A,(HEADER_DONE)
	AND	A
	JR	Z,.SCAN_HEADER
	JP	WRITE_BODY

.SCAN_HEADER
	LD	A,B
	OR	C
	RET	Z
	LD	A,(HL)
	INC	HL
	DEC	BC
	PUSH	AF
	CALL	CAPTURE_HEADER_CHAR
	POP	AF
	CALL	UPDATE_HEADER_STATE
	LD	A,(HEADER_DONE)
	AND	A
	JR	Z,.SCAN_HEADER
	JP	WRITE_BODY

CAPTURE_HEADER_CHAR
	PUSH	BC,DE,HL
	LD	E,A
	CP	13
	JR	Z,.DONE
	CP	10
	JR	Z,.LINE_DONE
	LD	A,(HEADER_LINE_LEN)
	CP	HEADER_LINE_SIZE-1
	JR	NC,.DONE
	LD	C,A
	LD	B,0
	LD	HL,HEADER_LINE
	ADD	HL,BC
	LD	(HL),E
	INC	A
	LD	(HEADER_LINE_LEN),A
	JR	.DONE
.LINE_DONE
	LD	A,(HEADER_LINE_LEN)
	LD	C,A
	LD	B,0
	LD	HL,HEADER_LINE
	ADD	HL,BC
	LD	(HL),0
	CALL	PARSE_HEADER_LINE
	XOR	A
	LD	(HEADER_LINE_LEN),A
.DONE
	POP	HL,DE,BC
	RET

PARSE_HEADER_LINE
	LD	HL,HEADER_LINE
	LD	DE,CONTENT_LENGTH_PREFIX
.MATCH
	LD	A,(DE)
	AND	A
	JR	Z,.FOUND
	CP	(HL)
	RET	NZ
	INC	DE
	INC	HL
	JR	.MATCH
.FOUND
	CALL	SKIP_HEADER_SPACES
	LD	A,(HL)
	CP	'0'
	RET	C
	CP	'9'+1
	RET	NC
	CALL	PARSE_CONTENT_LENGTH_U32
	LD	A,1
	LD	(CONTENT_LENGTH_SEEN),A
	RET

SKIP_HEADER_SPACES
	LD	A,(HL)
	CP	' '
	JR	Z,.NEXT
	CP	9
	RET	NZ
.NEXT
	INC	HL
	JR	SKIP_HEADER_SPACES

UPDATE_HEADER_STATE
	LD	E,A
	LD	A,(HDR_STATE)
	AND	A
	JR	Z,.STATE0
	CP	1
	JR	Z,.STATE1
	CP	2
	JR	Z,.STATE2

; State 3: CR LF CR seen, waiting for LF.
.STATE3
	LD	A,E
	CP	10
	JR	Z,.DONE
	CP	13
	JR	Z,.SET3
	XOR	A
	JR	.STORE
.STATE0
	LD	A,E
	CP	13
	JR	Z,.SET1
	XOR	A
	JR	.STORE
.STATE1
	LD	A,E
	CP	10
	JR	Z,.SET2
	CP	13
	JR	Z,.SET1
	XOR	A
	JR	.STORE
.STATE2
	LD	A,E
	CP	13
	JR	Z,.SET3
	XOR	A
	JR	.STORE
.SET1
	LD	A,1
	JR	.STORE
.SET2
	LD	A,2
	JR	.STORE
.SET3
	LD	A,3
	JR	.STORE
.DONE
	LD	A,1
	LD	(HEADER_DONE),A
	RET
.STORE
	LD	(HDR_STATE),A
	RET

WRITE_BODY
	LD	A,B
	OR	C
	RET	Z
	PUSH	BC
	CALL	WRITE_OUTPUT
	POP	BC
	RET	C
	CALL	ADD_BODY_BYTES
	LD	A,1
	LD	(HAVE_BODY),A
	LD	A,'.'
	JP	PUT_CHAR

ADD_BODY_BYTES
	LD	HL,(BODY_BYTES)
	ADD	HL,BC
	LD	(BODY_BYTES),HL
	RET	NC
	LD	HL,(BODY_BYTES+2)
	INC	HL
	LD	(BODY_BYTES+2),HL
	RET

CHECK_CONTENT_LENGTH
	LD	A,(CONTENT_LENGTH_SEEN)
	AND	A
	JR	Z,.OK
	LD	HL,CONTENT_LENGTH
	LD	DE,BODY_BYTES
	LD	B,4
.CMP
	LD	A,(DE)
	CP	(HL)
	JR	NZ,.MISMATCH
	INC	DE
	INC	HL
	DJNZ	.CMP
	JR	.OK
.MISMATCH
	PRINTLN MSG_INCOMPLETE
	LD	A,RES_RS_TIMEOUT
	SCF
	RET
.OK
	XOR	A
	RET

PARSE_CONTENT_LENGTH_U32
	XOR	A
	LD	(CONTENT_LENGTH),A
	LD	(CONTENT_LENGTH+1),A
	LD	(CONTENT_LENGTH+2),A
	LD	(CONTENT_LENGTH+3),A
.NEXT
	LD	A,(HL)
	CP	'0'
	RET	C
	CP	'9'+1
	RET	NC
	SUB	'0'
	LD	(U32_DIGIT),A
	CALL	U32_MUL10_CONTENT
	LD	A,(U32_DIGIT)
	CALL	U32_ADD_A_CONTENT
	INC	HL
	JR	.NEXT

U32_MUL10_CONTENT
	CALL	U32_COPY_CONTENT_TO_TEMP
	CALL	U32_SHL1_CONTENT
	CALL	U32_SHL1_TEMP
	CALL	U32_SHL1_TEMP
	CALL	U32_SHL1_TEMP
	JP	U32_ADD_TEMP_TO_CONTENT

U32_COPY_CONTENT_TO_TEMP
	LD	HL,CONTENT_LENGTH
	LD	DE,U32_TEMP
	LD	BC,4
.COPY
	LD	A,(HL)
	LD	(DE),A
	INC	HL
	INC	DE
	DEC	BC
	LD	A,B
	OR	C
	JR	NZ,.COPY
	RET

U32_SHL1_CONTENT
	LD	HL,CONTENT_LENGTH
	JR	U32_SHL1_AT_HL

U32_SHL1_TEMP
	LD	HL,U32_TEMP

U32_SHL1_AT_HL
	SLA	(HL)
	INC	HL
	RL	(HL)
	INC	HL
	RL	(HL)
	INC	HL
	RL	(HL)
	RET

U32_ADD_TEMP_TO_CONTENT
	LD	HL,CONTENT_LENGTH
	LD	DE,U32_TEMP
	LD	A,(DE)
	ADD	A,(HL)
	LD	(HL),A
	INC	HL
	INC	DE
	LD	A,(DE)
	ADC	A,(HL)
	LD	(HL),A
	INC	HL
	INC	DE
	LD	A,(DE)
	ADC	A,(HL)
	LD	(HL),A
	INC	HL
	INC	DE
	LD	A,(DE)
	ADC	A,(HL)
	LD	(HL),A
	RET

U32_ADD_A_CONTENT
	LD	E,A
	LD	D,0
	LD	HL,CONTENT_LENGTH
	LD	A,(HL)
	ADD	A,E
	LD	(HL),A
	INC	HL
	LD	A,(HL)
	ADC	A,D
	LD	(HL),A
	INC	HL
	LD	A,(HL)
	ADC	A,D
	LD	(HL),A
	INC	HL
	LD	A,(HL)
	ADC	A,D
	LD	(HL),A
	RET

PUT_CHAR
	PUSH	BC,HL
	LD	C,DSS_PUTCHAR
	RST	DSS
	POP	HL,BC
	RET

PRINT_HTTP_STATUS
	LD	HL,(HTTP_STATUS_PTR)
	LD	A,H
	OR	L
	RET	Z
	PRINT	MSG_HTTP_STATUS
	LD	HL,(HTTP_STATUS_PTR)
	LD	B,80
.NEXT
	LD	A,(HL)
	AND	A
	JR	Z,.DONE
	CP	13
	JR	Z,.DONE
	CP	10
	JR	Z,.DONE
	LD	C,DSS_PUTCHAR
	RST	DSS
	INC	HL
	DJNZ	.NEXT
.DONE
	PRINT	WCOMMON.LINE_END
	RET

APPEND_STR
	LD	A,(DE)
	AND	A
	RET	Z
	LD	(HL),A
	INC	HL
	INC	DE
	JR	APPEND_STR

APPEND_IX_STR
	LD	A,(IX+0)
	AND	A
	RET	Z
	LD	(HL),A
	INC	HL
	INC	IX
	JR	APPEND_IX_STR

MSG_START
	DB "WGET - HTTP downloader for SprinterESP",0
MSG_USAGE
	DB "Usage: WGET.EXE http://host[:port]/path FILE",0
MSG_WIFI_NOT_FOUND
	DB "Sprinter-WiFi not found!",0
MSG_UART_READY
	DB "UART initialized.",0
MSG_RESETTING_ESP
	DB "ESP did not answer, resetting module.",0
MSG_CONNECTING
	DB "Connecting to ",0
MSG_COLON
	DB ":",0
MSG_OUTPUT
	DB "Output file: ",0
MSG_DOWNLOADING
	DB "Downloading body:",0
MSG_NO_RESPONSE
	DB "No HTTP response received.",0
MSG_NO_BODY
	DB "HTTP response has no downloaded body.",0
MSG_DONE
	DB "WGET done.",0
MSG_NET_ERROR
	DB "Network/ESP error #"
MSG_ERROR_NO
	DB "n!",0
