; ======================================================
; WGET for Sprinter DSS Network Kit
; Minimal HTTP/1.0 downloader over ESP-AT TCP.
; ======================================================

EXE_VERSION		EQU 1
DEFAULT_TIMEOUT		EQU 2000
RECV_TIMEOUT		EQU 7000
RECV_BUFFER_SIZE	EQU 1500
URL_SIZE		EQU 160
HOST_SIZE		EQU 96
PORT_SIZE		EQU 8
PATH_SIZE		EQU 128
FILE_SIZE		EQU 80
REQ_SIZE		EQU 320
HEADER_LINE_SIZE	EQU 80
REDIRECT_LIMIT		EQU 5
DSS_CREATE_OVERWRITE	EQU 0x0A
NO_HANDLE		EQU 0xFF
HTTP_UNSUPPORTED_NONE	EQU 0
HTTP_UNSUPPORTED_REDIRECT EQU 1
HTTP_UNSUPPORTED_HTTPS	EQU 2
HTTP_UNSUPPORTED_CHUNKED EQU 3
HTTP_UNSUPPORTED_GZIP	EQU 4

	DEVICE NOSLOT64K

	INCLUDE "macro.inc"
	INCLUDE "dss.inc"

	MODULE MAIN

	; Full 512-byte DSS EXE header. Long command lines are written
	; at 0x8080, so these tools must not use the compact header.
	ORG 0x7F00

EXE_HEADER
	DB "EXE"
	DB EXE_VERSION
	DW 0x0200
	DW 0
	DW 0
	DW 0
	DW 0
	DW 0
	DW START
	DW START
	DW STACK_TOP
	DS 490, 0

	ORG 0x8100
@STACK_TOP

START
	CALL	CLEAR_BSS
	LD	A,NO_HANDLE
	LD	(OUT_FH),A
	XOR	A
	LD	(TCP_IS_OPEN),A

	CALL	ISA.ISA_RESET
	CALL	WCOMMON.INIT_VMODE
	PRINTLN MSG_START

	CALL	PARSE_CMD_LINE
	JP	C,USAGE
	CALL	PARSE_URL
	JP	C,USAGE
	CALL	SET_PORT_FLAG
	CALL	PRINT_SOURCE_INFO

	CALL	OPEN_OUTPUT_FILE
	JP	C,FILE_ERROR_EXIT

	CALL	WIFI.UART_FIND
	JP	C,NO_WIFI

	CALL	NETCFG.LOAD
	CALL	NETCFG.APPLY_UART_BAUD
	CALL	WIFI.UART_INIT
	PRINTLN MSG_UART_READY

	LD	HL,CMD_AT
	CALL	SEND_CMD_RECOVER

	LD	HL,CMD_ECHO_OFF
	CALL	SEND_CMD

	; Force ESP-AT to enable hardware RTS/CTS flow control. Without this,
	; ESP keeps streaming bytes during slow DSS_WRITE/file ops and the Z80
	; UART FIFO overruns (OE in LSR), causing +IPD parser desync and the
	; body-on-screen / Network error #0 / #4 symptoms.
	CALL	WCOMMON.SETUP_UART_FLOW
	AND	A
	JP	NZ,TCP_ERROR_EXIT

	LD	HL,CMD_CIPMUX_0
	CALL	SEND_CMD

.REQUEST_LOOP
	PRINT MSG_CONNECTING
	PRINT HOST_BUFF
	LD	A,(PORT_IS_DEFAULT)
	AND	A
	JR	NZ,.CONNECT_DEFAULT_PORT
	PRINT MSG_COLON
	PRINT PORT_BUFF
.CONNECT_DEFAULT_PORT
	PRINT WCOMMON.LINE_END
	LD	HL,HOST_BUFF
	LD	DE,PORT_BUFF
	CALL	TCP.OPEN
	JP	C,TCP_ERROR_EXIT
	LD	A,1
	LD	(TCP_IS_OPEN),A

	CALL	BUILD_HTTP_REQUEST
	LD	HL,REQ_BUFF
	CALL	UTIL.STRLEN
	CALL	TCP.SEND_BUFFER
	JP	C,TCP_ERROR_EXIT

	CALL	RECEIVE_HTTP
	JP	C,TCP_ERROR_EXIT

	; If RECEIVE_HTTP saw the remote close the connection, the ESP socket
	; is already gone. Skip CIPCLOSE — it would just sit waiting for an
	; OK/ERROR that the firmware may take a long time to deliver.
	LD	A,(SAW_CLOSED)
	AND	A
	JR	NZ,.SKIP_CLOSE
	LD	A,(LAST_TCP_ERR)
	CP	RES_NOT_CONN
	JR	Z,.SKIP_CLOSE
	CALL	TCP.CLOSE
.SKIP_CLOSE
	XOR	A
	LD	(TCP_IS_OPEN),A
	LD	A,(REDIRECT_PENDING)
	AND	A
	JR	Z,.REQUEST_DONE
	CALL	FOLLOW_REDIRECT
	JP	C,HTTP_ERROR_EXIT
	JR	.REQUEST_LOOP

.REQUEST_DONE
	CALL	CLOSE_OUTPUT_FILE
	JP	C,FILE_ERROR_EXIT

	PRINT WCOMMON.LINE_END
	CALL	PRINT_DOWNLOADED_BYTES
	PRINTLN MSG_DONE
	LD	B,0
	JP	WCOMMON.EXIT

NO_WIFI
	CALL	CLOSE_OUTPUT_FILE
	PRINTLN MSG_WIFI_NOT_FOUND
	LD	B,2
	JP	WCOMMON.EXIT

USAGE
	PRINTLN MSG_USAGE
	LD	B,1
	JP	WCOMMON.EXIT

TCP_ERROR_EXIT
	; Save inbound error code BEFORE reading CANCELLED — otherwise A is
	; clobbered and every network error gets printed as #0.
	PUSH	AF
	LD	A,(WCOMMON.CANCELLED)
	AND	A
	JR	Z,.NOT_CANCELLED
	POP	AF
	JP	CANCEL_EXIT
.NOT_CANCELLED
	LD	A,(TCP_IS_OPEN)
	AND	A
	CALL	NZ,TCP.CLOSE
	CALL	CLOSE_OUTPUT_FILE
	POP	AF
	ADD	A,'0'
	LD	(MSG_ERROR_NO),A
	PRINTLN MSG_NET_ERROR
	LD	B,3
	JP	WCOMMON.EXIT

FILE_ERROR_EXIT
	CALL	PRINT_FILE_ERROR
	LD	B,5
	JP	WCOMMON.EXIT

HTTP_ERROR_EXIT
	LD	A,(TCP_IS_OPEN)
	AND	A
	CALL	NZ,TCP.CLOSE
	CALL	CLOSE_OUTPUT_FILE
	CALL	PRINT_HTTP_STATUS
	CALL	PRINT_HTTP_UNSUPPORTED
	LD	B,3
	JP	WCOMMON.EXIT

CANCEL_EXIT
	LD	A,(TCP_IS_OPEN)
	AND	A
	CALL	NZ,TCP.CLOSE
	CALL	CLOSE_OUTPUT_FILE
	PRINT	WCOMMON.LINE_END
	PRINTLN	MSG_CANCELLED
	LD	B,7
	JP	WCOMMON.EXIT

; ------------------------------------------------------
; Parse command line: WGET.EXE http://host[:port]/path FILE
; ------------------------------------------------------
PARSE_CMD_LINE
	LD	HL,0x8080
	LD	A,(HL)
	AND	A
	JR	Z,.ERR
	LD	B,A
	INC	HL
	CALL	SKIP_SPACES
	JR	C,.ERR
	LD	DE,URL_BUFF
	LD	C,URL_SIZE-1
	CALL	COPY_ARG
	JR	C,.ERR
	CALL	SKIP_SPACES
	JR	C,.ERR
	LD	DE,OUT_FILE
	LD	C,FILE_SIZE-1
	CALL	COPY_ARG
	RET	NC
.ERR
	SCF
	RET

SKIP_SPACES
	LD	A,B
	AND	A
	JR	Z,.ERR
	LD	A,(HL)
	CP	0x21
	RET	NC
	INC	HL
	DJNZ	SKIP_SPACES
.ERR
	SCF
	RET

COPY_ARG
	XOR	A
	LD	(ARG_LEN),A
.NEXT
	LD	A,B
	AND	A
	JR	Z,.END
	LD	A,(HL)
	CP	0x21
	JR	C,.END
	LD	A,C
	AND	A
	JR	Z,.ERR
	LD	A,(HL)
	LD	(DE),A
	INC	DE
	INC	HL
	DEC	B
	DEC	C
	LD	A,(ARG_LEN)
	INC	A
	LD	(ARG_LEN),A
	JR	.NEXT
.END
	XOR	A
	LD	(DE),A
	LD	A,(ARG_LEN)
	AND	A
	RET	NZ
.ERR
	SCF
	RET

; ------------------------------------------------------
; Parse URL_BUFF into HOST_BUFF, PORT_BUFF and PATH_BUFF.
; ------------------------------------------------------
PARSE_URL
	XOR	A
	LD	(URL_SCHEME_ADDED),A
	LD	HL,URL_BUFF
	LD	DE,HTTP_PREFIX
	CALL	UTIL.STARTSWITH
	JR	Z,.HAS_HTTP
	LD	A,1
	LD	(URL_SCHEME_ADDED),A
	LD	HL,URL_BUFF
	JR	.PARSE_HOST
.HAS_HTTP
	LD	BC,7
	ADD	HL,BC

.PARSE_HOST
	LD	DE,HOST_BUFF
	LD	C,HOST_SIZE-1
.HOST
	LD	A,(HL)
	AND	A
	JR	Z,.HOST_DONE
	CP	'/'
	JR	Z,.HOST_DONE
	CP	':'
	JR	Z,.PORT_START
	LD	A,C
	AND	A
	JR	Z,.ERR
	LD	A,(HL)
	LD	(DE),A
	INC	DE
	INC	HL
	DEC	C
	JR	.HOST

.HOST_DONE
	XOR	A
	LD	(DE),A
	LD	A,(HOST_BUFF)
	AND	A
	JR	Z,.ERR
	PUSH	HL
	LD	DE,DEFAULT_PORT
	CALL	COPY_PORT_DEFAULT
	POP	HL
	JR	.PATH_START

.PORT_START
	XOR	A
	LD	(DE),A
	INC	HL
	LD	DE,PORT_BUFF
	LD	C,PORT_SIZE-1
.PORT
	LD	A,(HL)
	AND	A
	JR	Z,.PORT_DONE
	CP	'/'
	JR	Z,.PORT_DONE
	CP	'0'
	JR	C,.ERR
	CP	'9'+1
	JR	NC,.ERR
	LD	A,C
	AND	A
	JR	Z,.ERR
	LD	A,(HL)
	LD	(DE),A
	INC	DE
	INC	HL
	DEC	C
	JR	.PORT
.PORT_DONE
	XOR	A
	LD	(DE),A
	LD	A,(PORT_BUFF)
	AND	A
	JR	Z,.ERR

.PATH_START
	LD	A,(HL)
	CP	'/'
	JR	Z,.COPY_PATH
	LD	HL,DEFAULT_PATH
	LD	DE,PATH_BUFF
	JP	COPY_ASCIIZ_DE

.COPY_PATH
	LD	DE,PATH_BUFF
	LD	C,PATH_SIZE-1
.PATH
	LD	A,(HL)
	AND	A
	JR	Z,.PATH_DONE
	LD	A,C
	AND	A
	JR	Z,.ERR
	LD	A,(HL)
	LD	(DE),A
	INC	DE
	INC	HL
	DEC	C
	JR	.PATH
.PATH_DONE
	XOR	A
	LD	(DE),A
	RET
.ERR
	SCF
	RET

COPY_PORT_DEFAULT
	LD	HL,DEFAULT_PORT
	LD	DE,PORT_BUFF
	JP	COPY_ASCIIZ_DE

COPY_ASCIIZ_DE
	LD	A,(HL)
	LD	(DE),A
	AND	A
	RET	Z
	INC	HL
	INC	DE
	JR	COPY_ASCIIZ_DE

; ------------------------------------------------------
; Print source URL details after parsing.
; ------------------------------------------------------
PRINT_SOURCE_INFO
	PRINT	MSG_SOURCE
	PRINT	URL_BUFF
	PRINT	WCOMMON.LINE_END
	LD	A,(URL_SCHEME_ADDED)
	AND	A
	JR	Z,.NO_WARN
	PRINTLN	MSG_SCHEME_ADDED
.NO_WARN
	RET

; ------------------------------------------------------
; Clear runtime-only buffers placed after the loaded EXE image.
; ------------------------------------------------------
CLEAR_BSS
	LD	HL,WGET_BSS_BASE
	LD	DE,WGET_BSS_BASE+1
	LD	BC,WGET_BSS_END-WGET_BSS_BASE-1
	LD	(HL),0
	LDIR
	RET

; ------------------------------------------------------
; Local output file helpers.
; ------------------------------------------------------
OPEN_OUTPUT_FILE
	PRINT MSG_OUTPUT
	PRINT OUT_FILE
	PRINT WCOMMON.LINE_END
	LD	HL,OUT_FILE
	LD	A,FA_ARCHIVE
	LD	C,DSS_CREATE_OVERWRITE
	RST	DSS
	RET	C
	LD	(OUT_FH),A
	RET

CLOSE_OUTPUT_FILE
	LD	A,(OUT_FH)
	CP	NO_HANDLE
	RET	Z
	LD	C,DSS_CLOSE_FILE
	RST	DSS
	LD	A,NO_HANDLE
	LD	(OUT_FH),A
	RET

WRITE_OUTPUT
	LD	A,B
	OR	C
	RET	Z
	LD	A,(OUT_FH)
	LD	D,B
	LD	E,C
	LD	C,DSS_WRITE
	RST	DSS
	RET

PRINT_FILE_ERROR
	PRINT	MSG_FILE_ERROR
	CALL	DSS_ERROR.GET_ERR_MSG
	PRINTLN_HL
	RET

; ------------------------------------------------------
; ESP/TCP command helpers.
; ------------------------------------------------------
SEND_CMD
	LD	DE,WIFI.RS_BUFF
	LD	BC,DEFAULT_TIMEOUT
	CALL	WIFI.UART_TX_CMD
	AND	A
	RET	Z
	JP	TCP_ERROR_EXIT

SEND_CMD_RECOVER
	PUSH	HL
	LD	DE,WIFI.RS_BUFF
	LD	BC,DEFAULT_TIMEOUT
	CALL	WIFI.UART_TX_CMD
	AND	A
	JR	Z,.OK

	PRINTLN MSG_RESETTING_ESP
	CALL	WIFI.ESP_RESET
	CALL	WIFI.UART_SET_DEFAULT_DIVISOR
	CALL	WIFI.UART_INIT
	POP	HL
	JP	SEND_CMD
.OK
	POP	HL
	RET

; ------------------------------------------------------
; Build HTTP/1.0 GET request in REQ_BUFF.
; ------------------------------------------------------
BUILD_HTTP_REQUEST
	CALL	SET_PORT_FLAG
	LD	HL,REQ_BUFF
	LD	DE,REQ_GET
	CALL	APPEND_STR
	LD	IX,PATH_BUFF
	CALL	APPEND_IX_STR
	LD	DE,REQ_HTTP
	CALL	APPEND_STR
	LD	IX,HOST_BUFF
	CALL	APPEND_IX_STR
	LD	A,(PORT_IS_DEFAULT)
	AND	A
	JR	NZ,.NO_HOST_PORT
	LD	DE,REQ_PORT_SEP
	CALL	APPEND_STR
	LD	IX,PORT_BUFF
	CALL	APPEND_IX_STR
.NO_HOST_PORT
	LD	DE,REQ_END
	JP	APPEND_STR

; ------------------------------------------------------
; Set PORT_IS_DEFAULT=1 when port is "80", otherwise 0.
; ------------------------------------------------------
SET_PORT_FLAG
	LD	HL,DEFAULT_PORT
	LD	DE,PORT_BUFF
	CALL	UTIL.STRCMP
	JR	C,.CUSTOM
	LD	A,1
	LD	(PORT_IS_DEFAULT),A
	RET
.CUSTOM
	XOR	A
	LD	(PORT_IS_DEFAULT),A
	RET

; ------------------------------------------------------
; Receive HTTP response, skip headers and write body to file.
; ------------------------------------------------------
RECEIVE_HTTP
	XOR	A
	LD	(GOT_RESPONSE),A
	LD	(HEADER_DONE),A
	LD	(HDR_STATE),A
	LD	(HAVE_BODY),A
	LD	(HTTP_STATUS_PTR),A
	LD	(HTTP_STATUS_PTR+1),A
	LD	(ADJUST_RESPONSE),A
	LD	(CONTENT_LENGTH),A
	LD	(CONTENT_LENGTH+1),A
	LD	(CONTENT_LENGTH+2),A
	LD	(CONTENT_LENGTH+3),A
	LD	(CONTENT_LENGTH_SEEN),A
	LD	(BODY_BYTES),A
	LD	(BODY_BYTES+1),A
	LD	(BODY_BYTES+2),A
	LD	(BODY_BYTES+3),A
	LD	(BODY_LOG_PRINTED),A
	LD	(DEBUG_PRINTED),A
	LD	(HEADER_LINE_LEN),A
	LD	(SAW_CLOSED),A
	LD	(HTTP_STATUS_CLASS),A
	LD	(REDIRECT_PENDING),A
	LD	(LOCATION_SEEN),A
	LD	(HTTP_UNSUPPORTED_REASON),A

.LOOP
	CALL	WCOMMON.CHECK_CANCEL
	JP	C,.CANCELLED
	CALL	WIFI.UART_RX_RESUME
	LD	HL,RECV_BUFFER
	LD	BC,RECV_BUFFER_SIZE
	LD	DE,RECV_TIMEOUT
	CALL	TCP.RECEIVE
	; Pause RX immediately so ESP stops sending while we run the (slow)
	; HTTP parser, debug print, file write etc. Resumed at .LOOP top.
	PUSH	AF,BC
	CALL	WIFI.UART_RX_PAUSE
	CALL	STORE_RECV_DEBUG
	POP	BC,AF
	JR	C,.RX_ERROR
	CALL	UART_RX_ERROR_SEEN
	JP	NZ,.RX_UART_ERROR
	LD	A,B
	OR	C
	JR	Z,.RX_DONE
	CALL	TRIM_CLOSED_SUFFIX
	LD	A,B
	OR	C
	JR	Z,.RX_DONE

	PUSH	BC
	LD	A,(GOT_RESPONSE)
	AND	A
	JR	NZ,.STATUS_DONE
	LD	A,1
	LD	(GOT_RESPONSE),A
	LD	HL,RECV_BUFFER
	CALL	CHECK_HTTP_STATUS
	JR	C,.HTTP_ERR_POP
	LD	A,1
	LD	(ADJUST_RESPONSE),A
.STATUS_DONE
	POP	BC
	LD	A,(ADJUST_RESPONSE)
	AND	A
	JR	Z,.PROCESS_FROM_START
	XOR	A
	LD	(ADJUST_RESPONSE),A
	CALL	ADJUST_TO_HTTP_STATUS
	JR	.PROCESS_DATA
.PROCESS_FROM_START
	LD	HL,RECV_BUFFER
.PROCESS_DATA
	CALL	PROCESS_HTTP_DATA
	JR	C,.PROCESS_ERR
	LD	A,(SAW_CLOSED)
	AND	A
	JR	NZ,.RX_DONE
	JR	.LOOP

.PROCESS_ERR
	LD	A,(HTTP_UNSUPPORTED_REASON)
	AND	A
	JP	NZ,HTTP_ERROR_EXIT
	JR	.ERR

.HTTP_ERR_POP
	POP	BC
	JP	HTTP_ERROR_EXIT

.RX_DONE
	LD	A,(REDIRECT_PENDING)
	AND	A
	JR	NZ,.REDIRECT_DONE
	XOR	A
	LD	(LAST_TCP_ERR),A
.RX_ERROR_DONE
	LD	A,(HAVE_BODY)
	AND	A
	JR	Z,.NO_BODY
	JP	CHECK_CONTENT_LENGTH
.RX_ERROR
	LD	(LAST_TCP_ERR),A
	CP	RES_NOT_CONN
	JR	NZ,.RX_ERROR_NOT_CLOSED
	LD	A,1
	LD	(SAW_CLOSED),A
.RX_ERROR_NOT_CLOSED
	PRINT	WCOMMON.LINE_END
	CALL	PRINT_RECV_DEBUG
	CALL	PRINT_UART_OVERRUN_HINT
	LD	A,1
	LD	(DEBUG_PRINTED),A
	JR	.RX_ERROR_DONE
.RX_UART_ERROR
	LD	A,RES_RS_TIMEOUT
	JR	.RX_ERROR
.NO_BODY
	LD	A,(GOT_RESPONSE)
	AND	A
	JR	Z,.NO_RESPONSE
	PRINTLN MSG_NO_BODY
	JR	.NO_BODY_ERR
.NO_RESPONSE
	PRINTLN MSG_NO_RESPONSE
.NO_BODY_ERR
	LD	A,(LAST_TCP_ERR)
	AND	A
	JR	NZ,.ERR
	LD	A,RES_RS_TIMEOUT
.ERR
	SCF
	RET
.REDIRECT_DONE
	XOR	A
	RET
.CANCELLED
	JP	CANCEL_EXIT

CHECK_HTTP_STATUS
	LD	A,B
	OR	C
	JR	Z,.ERR

.FIND_STATUS
	LD	A,B
	OR	A
	JR	NZ,.ENOUGH_BYTES
	LD	A,C
	CP	5
	JR	C,.ERR
.ENOUGH_BYTES
	CALL	MATCH_HTTP_PREFIX
	JR	Z,.FOUND
	INC	HL
	DEC	BC
	JR	.FIND_STATUS

.FOUND
	LD	(HTTP_STATUS_PTR),HL
	CALL	HTTP_STATUS_ACCEPTED
	RET	NC
.ERR
	SCF
	RET

MATCH_HTTP_PREFIX
	PUSH	HL,DE,BC
	LD	DE,HTTP_STATUS_PREFIX
	LD	B,5
.NEXT
	LD	A,(DE)
	CP	(HL)
	JR	NZ,.NO_MATCH
	INC	DE
	INC	HL
	DJNZ	.NEXT
	XOR	A
	POP	BC,DE,HL
	RET
.NO_MATCH
	LD	A,1
	OR	A
	POP	BC,DE,HL
	RET

HTTP_STATUS_ACCEPTED
	LD	B,16
.FIND_SPACE
	LD	A,(HL)
	CP	' '
	JR	Z,.SKIP_SPACES
	CP	13
	JR	Z,.ERR
	CP	10
	JR	Z,.ERR
	AND	A
	JR	Z,.ERR
	INC	HL
	DJNZ	.FIND_SPACE
	JR	.ERR
.SKIP_SPACES
	INC	HL
	LD	A,(HL)
	CP	' '
	JR	Z,.SKIP_SPACES
	LD	(HTTP_STATUS_CLASS),A
	CP	'2'
	RET	Z
	CP	'3'
	RET	Z
.ERR
	SCF
	RET

ADJUST_TO_HTTP_STATUS
	LD	DE,(HTTP_STATUS_PTR)
	PUSH	DE
	LD	HL,RECV_BUFFER
	ADD	HL,BC
	POP	DE
	AND	A
	SBC	HL,DE
	LD	B,H
	LD	C,L
	EX	DE,HL
	RET

PROCESS_HTTP_DATA
	LD	A,(HEADER_DONE)
	AND	A
	JR	Z,.SCAN_HEADER
	CALL	CHECK_REDIRECT_HEADER
	RET	C
	LD	A,(REDIRECT_PENDING)
	AND	A
	RET	NZ
	CALL	CHECK_UNSUPPORTED_HTTP
	RET	C
	JP	WRITE_BODY

.SCAN_HEADER
	LD	A,B
	OR	C
	RET	Z
	LD	A,(HL)
	INC	HL
	DEC	BC
	PUSH	AF
	CALL	CAPTURE_HEADER_CHAR
	POP	AF
	CALL	UPDATE_HEADER_STATE
	LD	A,(HEADER_DONE)
	AND	A
	JR	Z,.SCAN_HEADER
	CALL	CHECK_REDIRECT_HEADER
	RET	C
	LD	A,(REDIRECT_PENDING)
	AND	A
	RET	NZ
	CALL	CHECK_UNSUPPORTED_HTTP
	RET	C
	JP	WRITE_BODY

CAPTURE_HEADER_CHAR
	PUSH	BC,DE,HL
	LD	E,A
	CP	13
	JR	Z,.DONE
	CP	10
	JR	Z,.LINE_DONE
	LD	A,(HEADER_LINE_LEN)
	CP	HEADER_LINE_SIZE-1
	JR	NC,.DONE
	LD	C,A
	LD	B,0
	LD	HL,HEADER_LINE
	ADD	HL,BC
	LD	(HL),E
	INC	A
	LD	(HEADER_LINE_LEN),A
	JR	.DONE
.LINE_DONE
	LD	A,(HEADER_LINE_LEN)
	LD	C,A
	LD	B,0
	LD	HL,HEADER_LINE
	ADD	HL,BC
	LD	(HL),0
	CALL	PARSE_HEADER_LINE
	XOR	A
	LD	(HEADER_LINE_LEN),A
.DONE
	POP	HL,DE,BC
	RET

PARSE_HEADER_LINE
	LD	HL,HEADER_LINE
	LD	DE,CONTENT_LENGTH_PREFIX
	CALL	MATCH_HEADER_PREFIX
	JR	NZ,.TRY_TRANSFER_ENCODING
.FOUND
	CALL	SKIP_HEADER_SPACES
	LD	A,(HL)
	CP	'0'
	RET	C
	CP	'9'+1
	RET	NC
	CALL	PARSE_CONTENT_LENGTH_U32
	LD	A,1
	LD	(CONTENT_LENGTH_SEEN),A
	RET

.TRY_TRANSFER_ENCODING
	LD	HL,HEADER_LINE
	LD	DE,TRANSFER_ENCODING_PREFIX
	CALL	MATCH_HEADER_PREFIX
	JR	NZ,.TRY_CONTENT_ENCODING
	CALL	SKIP_HEADER_SPACES
	LD	DE,CHUNKED_VALUE
	CALL	MATCH_TOKEN_CI
	RET	NZ
	LD	A,HTTP_UNSUPPORTED_CHUNKED
	LD	(HTTP_UNSUPPORTED_REASON),A
	RET

.TRY_CONTENT_ENCODING
	LD	HL,HEADER_LINE
	LD	DE,CONTENT_ENCODING_PREFIX
	CALL	MATCH_HEADER_PREFIX
	JR	NZ,.TRY_LOCATION
	CALL	SKIP_HEADER_SPACES
	LD	DE,GZIP_VALUE
	CALL	MATCH_TOKEN_CI
	RET	NZ
	LD	A,HTTP_UNSUPPORTED_GZIP
	LD	(HTTP_UNSUPPORTED_REASON),A
	RET

.TRY_LOCATION
	LD	HL,HEADER_LINE
	LD	DE,LOCATION_PREFIX
	CALL	MATCH_HEADER_PREFIX
	RET	NZ
.LOC_FOUND
	CALL	SKIP_HEADER_SPACES
	LD	DE,LOCATION_BUFF
	LD	C,URL_SIZE-1
	CALL	COPY_HEADER_VALUE
	LD	A,1
	LD	(LOCATION_SEEN),A
	RET

MATCH_HEADER_PREFIX
	LD	A,(DE)
	AND	A
	RET	Z
	CP	(HL)
	RET	NZ
	INC	DE
	INC	HL
	JR	MATCH_HEADER_PREFIX

MATCH_TOKEN_CI
	LD	A,(DE)
	AND	A
	RET	Z
	LD	C,A
	LD	A,(HL)
	CALL	ASCII_TO_LOWER
	CP	C
	RET	NZ
	INC	HL
	INC	DE
	JR	MATCH_TOKEN_CI

ASCII_TO_LOWER
	CP	'A'
	RET	C
	CP	'Z'+1
	RET	NC
	ADD	A,32
	RET

SKIP_HEADER_SPACES
	LD	A,(HL)
	CP	' '
	JR	Z,.NEXT
	CP	9
	RET	NZ
.NEXT
