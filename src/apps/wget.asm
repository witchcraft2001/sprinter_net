	PRINT	NUM_PRINT_BUFF
	RET

ADD_BODY_BYTES
	LD	HL,(BODY_BYTES)
	ADD	HL,BC
	LD	(BODY_BYTES),HL
	RET	NC
	LD	HL,(BODY_BYTES+2)
	INC	HL
	LD	(BODY_BYTES+2),HL
	RET

CHECK_CONTENT_LENGTH
	LD	A,(CONTENT_LENGTH_SEEN)
	AND	A
	JR	Z,.OK
	LD	HL,CONTENT_LENGTH
	LD	DE,BODY_BYTES
	LD	B,4
.CMP
	LD	A,(DE)
	CP	(HL)
	JR	NZ,.MISMATCH
	INC	DE
	INC	HL
	DJNZ	.CMP
	JR	.OK
.MISMATCH
	PRINT	WCOMMON.LINE_END
	PRINTLN MSG_INCOMPLETE
	PRINT	MSG_EXPECTED
	LD	HL,CONTENT_LENGTH
	CALL	PRINT_U32_AT_HL
	PRINTLN	MSG_BYTES
	CALL	PRINT_DOWNLOADED_BYTES
	LD	A,(DEBUG_PRINTED)
	AND	A
	JR	NZ,.DEBUG_DONE
	CALL	PRINT_RECV_DEBUG
.DEBUG_DONE
	CALL	PRINT_UART_OVERRUN_HINT
	LD	A,RES_RS_TIMEOUT
	SCF
	RET
.OK
	XOR	A
	RET

PRINT_UART_OVERRUN_HINT
	LD	A,(TCP.LSR_ACCUM)
	AND	LSR_OE
	RET	Z
	PRINTLN	MSG_UART_OVERRUN
	RET

UART_RX_ERROR_SEEN
	LD	A,(TCP.LSR_ACCUM)
	AND	LSR_OE | LSR_PE | LSR_FE | LSR_BI | LSR_RCVE
	RET

PARSE_CONTENT_LENGTH_U32
	XOR	A
	LD	(CONTENT_LENGTH),A
	LD	(CONTENT_LENGTH+1),A
	LD	(CONTENT_LENGTH+2),A
	LD	(CONTENT_LENGTH+3),A
.NEXT
	LD	A,(HL)
	CP	'0'
	RET	C
	CP	'9'+1
	RET	NC
	SUB	'0'
	PUSH	HL
	LD	(U32_DIGIT),A
	CALL	U32_MUL10_CONTENT
	LD	A,(U32_DIGIT)
	CALL	U32_ADD_A_CONTENT
	POP	HL
	INC	HL
	JR	.NEXT

U32_MUL10_CONTENT
	CALL	U32_COPY_CONTENT_TO_TEMP
	CALL	U32_SHL1_CONTENT
	CALL	U32_SHL1_TEMP
	CALL	U32_SHL1_TEMP
	CALL	U32_SHL1_TEMP
	JP	U32_ADD_TEMP_TO_CONTENT

U32_COPY_CONTENT_TO_TEMP
	LD	HL,CONTENT_LENGTH
	LD	DE,U32_TEMP
	LD	BC,4
.COPY
	LD	A,(HL)
	LD	(DE),A
	INC	HL
	INC	DE
	DEC	BC
	LD	A,B
	OR	C
	JR	NZ,.COPY
	RET

