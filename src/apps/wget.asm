; ------------------------------------------------------
; Remove ESP-AT connection-close notification from the end of a receive block.
; Some ESP/jesperl streams append CLOSED after TCP payload bytes. It is not
; HTTP body and must never be written to the output file.
;
; Guard: only attempt the trim when we are past HTTP headers AND a
; Content-Length is known AND BODY_BYTES + BC strictly exceeds
; Content-Length. Without that, an HTTP body that legitimately ends with
; "CLOSED" plus CR/LF (text payload, log file, etc.) would be silently
; truncated. With the guard we only ever trim bytes that already overflow
; the announced body size.
; In/Out: BC - received byte count.
; ------------------------------------------------------
TRIM_CLOSED_SUFFIX
	LD	(TRIM_ORIG_LEN),BC
	LD	(TRIM_CAND_LEN),BC
	LD	A,(HEADER_DONE)
	AND	A
	JR	Z,.NO_MATCH
	LD	A,(CONTENT_LENGTH_SEEN)
	AND	A
	JR	Z,.NO_MATCH
	CALL	BODY_PLUS_BC_OVER_CL
	JR	NC,.NO_MATCH
	LD	HL,RECV_BUFFER
	ADD	HL,BC
.SKIP_TAIL_CRLF
	LD	BC,(TRIM_CAND_LEN)
	LD	A,B
	OR	C
	JR	Z,.NO_MATCH
	DEC	HL
	LD	A,(HL)
	CP	13
	JR	Z,.DROP_TAIL
	CP	10
	JR	Z,.DROP_TAIL
	INC	HL
	JR	.CHECK_CLOSED
.DROP_TAIL
	DEC	BC
	LD	(TRIM_CAND_LEN),BC
	JR	.SKIP_TAIL_CRLF
.CHECK_CLOSED
	LD	BC,(TRIM_CAND_LEN)
	LD	A,B
	AND	A
	JR	NZ,.HAVE_ENOUGH
	LD	A,C
	CP	6
	JR	C,.NO_MATCH
.HAVE_ENOUGH
	PUSH	HL
	LD	DE,6
	AND	A
	SBC	HL,DE
	LD	DE,CLOSED_WORD
	LD	B,6
.MATCH
	LD	A,(DE)
	CP	(HL)
	JR	NZ,.RESTORE_NO_MATCH
	INC	DE
	INC	HL
	DJNZ	.MATCH
	POP	HL
	LD	HL,(TRIM_CAND_LEN)
	LD	DE,6
	AND	A
	SBC	HL,DE
	LD	B,H
	LD	C,L
	LD	A,1
	LD	(SAW_CLOSED),A
	RET
.RESTORE_NO_MATCH
	POP	HL
.NO_MATCH
	LD	BC,(TRIM_ORIG_LEN)
	RET

; ------------------------------------------------------
; CF=1 iff (BODY_BYTES + BC) > CONTENT_LENGTH (32-bit unsigned, strict).
; Preserves BC. Trashes A, DE, HL.
; ------------------------------------------------------
BODY_PLUS_BC_OVER_CL
	PUSH	BC
	LD	HL,(BODY_BYTES)
	ADD	HL,BC
	LD	(TRIM_TMP),HL
	LD	HL,(BODY_BYTES+2)
	JR	NC,.NO_CARRY
	INC	HL
.NO_CARRY
	LD	(TRIM_TMP+2),HL

	; Compare 32-bit TRIM_TMP > CONTENT_LENGTH; high words first.
	LD	HL,(CONTENT_LENGTH+2)
	LD	DE,(TRIM_TMP+2)
	OR	A
	SBC	HL,DE
	JR	C,.OVER				; CL_hi < TMP_hi -> over
	JR	NZ,.NOT_OVER			; CL_hi > TMP_hi -> not over
	; equal high words; compare low
	LD	HL,(CONTENT_LENGTH)
	LD	DE,(TRIM_TMP)
	OR	A
	SBC	HL,DE
	JR	C,.OVER				; CL_lo < TMP_lo -> over
	; CL_lo >= TMP_lo (including equal) -> not strictly over
.NOT_OVER
	POP	BC
	AND	A
	RET
.OVER
	POP	BC
	SCF
	RET

UPDATE_HEADER_STATE
	LD	E,A
	LD	A,(HDR_STATE)
	AND	A
	JR	Z,.STATE0
	CP	1
	JR	Z,.STATE1
	CP	2
	JR	Z,.STATE2

; State 3: CR LF CR seen, waiting for LF.
.STATE3
	LD	A,E
	CP	10
	JR	Z,.DONE
	CP	13
	JR	Z,.SET3
	XOR	A
	JR	.STORE
.STATE0
	LD	A,E
	CP	13
	JR	Z,.SET1
	XOR	A
	JR	.STORE
.STATE1
	LD	A,E
	CP	10
	JR	Z,.SET2
	CP	13
	JR	Z,.SET1
	XOR	A
	JR	.STORE
.STATE2
	LD	A,E
	CP	13
	JR	Z,.SET3
	XOR	A
	JR	.STORE
.SET1
	LD	A,1
	JR	.STORE
.SET2
	LD	A,2
	JR	.STORE
.SET3
	LD	A,3
	JR	.STORE
.DONE
	LD	A,1
	LD	(HEADER_DONE),A
	RET
.STORE
	LD	(HDR_STATE),A
	RET

WRITE_BODY
	; RX is already paused by RECEIVE_HTTP main loop; resume happens at .LOOP top.
	LD	A,B
	OR	C
	RET	Z
	LD	A,(BODY_LOG_PRINTED)
	AND	A
	JR	NZ,.LOG_DONE
	PUSH	HL
	PUSH	BC
	CALL	Z,PRINT_BODY_LOG_HEADER
	POP	BC
	POP	HL
.LOG_DONE
	PUSH	BC
	CALL	WRITE_OUTPUT
	POP	BC
	RET	C
	CALL	ADD_BODY_BYTES
	LD	A,'.'
	CALL	PUT_CHAR
	LD	A,1
	LD	(HAVE_BODY),A
	RET

PRINT_BODY_LOG_HEADER
	LD	A,1
	LD	(BODY_LOG_PRINTED),A
	LD	A,(CONTENT_LENGTH_SEEN)
	AND	A
	JR	Z,.UNKNOWN
	PRINT	MSG_CONTENT_LENGTH
	LD	HL,CONTENT_LENGTH
	CALL	PRINT_U32_AT_HL
	PRINTLN	MSG_BYTES
	JR	.DOWNLOAD
.UNKNOWN
	PRINTLN	MSG_CONTENT_UNKNOWN
.DOWNLOAD
	PRINT	MSG_DOWNLOADING
	RET

PRINT_DOWNLOADED_BYTES
	PRINT	MSG_DOWNLOADED
	LD	HL,BODY_BYTES
	CALL	PRINT_U32_AT_HL
	PRINTLN	MSG_BYTES
	RET

STORE_RECV_DEBUG
	LD	(DEBUG_RECV_RESULT),A
	LD	(DEBUG_RECV_BYTES),BC
	AND	A
	JR	Z,.BYTES_READY
	LD	HL,(TCP.RECV_STORED)
	LD	(DEBUG_RECV_BYTES),HL
.BYTES_READY
	LD	A,(DEBUG_RECV_RESULT)
	RET

PRINT_RECV_DEBUG
	PRINT	MSG_DBG_RECV
	LD	A,(DEBUG_RECV_RESULT)
	CALL	PRINT_A_DEC
	PRINT	MSG_DBG_GOT
	LD	HL,(DEBUG_RECV_BYTES)
	CALL	PRINT_HL_DEC
	PRINT	MSG_DBG_IPD
	LD	HL,(TCP.LAST_IPD_LEN)
	CALL	PRINT_HL_DEC
	PRINT	MSG_DBG_LEFT
	LD	HL,(TCP.PAYLOAD_LEFT)
	CALL	PRINT_HL_DEC
	PRINT	MSG_DBG_LSR
	LD	A,(TCP.LAST_LSR)
	LD	C,A
	LD	DE,DEBUG_LSR_HEX
	CALL	UTIL.HEXB
	PRINT	DEBUG_LSR_HEX
	PRINT	MSG_DBG_LSR_ACC
	LD	A,(TCP.LSR_ACCUM)
	LD	C,A
	LD	DE,DEBUG_LSR_ACC_HEX
	CALL	UTIL.HEXB
	PRINT	DEBUG_LSR_ACC_HEX
	PRINT	MSG_DBG_TOTAL
	LD	HL,BODY_BYTES
	CALL	PRINT_U32_AT_HL
	PRINT	WCOMMON.LINE_END
	RET

PRINT_A_DEC
