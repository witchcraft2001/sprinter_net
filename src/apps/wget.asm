	ADD	HL,BC
	LD	(BODY_BYTES),HL
	JR	NC,.ADD_TOTAL
	LD	HL,(BODY_BYTES+2)
	INC	HL
	LD	(BODY_BYTES+2),HL
.ADD_TOTAL
