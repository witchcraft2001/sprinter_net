	LD	E,A
	LD	A,(BODY_BYTES+1)
	OR	E
	LD	E,A
	LD	A,(BODY_BYTES+2)
	OR	E
	LD	E,A
	LD	A,(BODY_BYTES+3)
	OR	E
	JR	Z,.GIVE_UP

	LD	A,(RESUME_COUNT)
	INC	A
	LD	(RESUME_COUNT),A

	LD	A,(TCP_IS_OPEN)
	AND	A
	JR	Z,.CLOSED
	LD	A,(SAW_CLOSED)
	AND	A
	CALL	Z,TCP.CLOSE
.CLOSED
	XOR	A
	LD	(TCP_IS_OPEN),A
	LD	(HTTP_INCOMPLETE),A

	PRINT	WCOMMON.LINE_END
	PRINT	MSG_RESUME
	LD	HL,TOTAL_BYTES
	CALL	PRINT_U32_AT_HL
	PRINTLN	MSG_BYTES
	XOR	A
	RET
.GIVE_UP
	CALL	PRINT_CONTENT_LENGTH_MISMATCH
	RET

TOTAL_BYTES_IS_ZERO
