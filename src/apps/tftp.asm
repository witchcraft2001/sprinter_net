START
	CALL	CLEAR_BSS
	LD	A,NO_HANDLE
	LD	(OUT_FH),A
	LD	(IN_FH),A
	XOR	A
	LD	(UDP_IS_OPEN),A
	LD	(TRANSFER_DONE),A
