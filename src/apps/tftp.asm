	JP	C,TRANSFER_ERROR_EXIT

	CALL	UDP.CLOSE
	XOR	A
	LD	(UDP_IS_OPEN),A
	CALL	CLOSE_FILES
	JP	C,FILE_ERROR_EXIT

	PRINT WCOMMON.LINE_END
	PRINTLN MSG_DONE
	LD	B,0
	JP	WCOMMON.EXIT

NO_WIFI
