	MODULE MAIN

; Place TFTP buffers AFTER TCP/UDP buffers, not just after NETCFG. The
; ESP_TCP_BSS_BASE override (0xB000) puts TCP/UDP CMD buffers inside the
; TFTP BSS range — HOST_BUFF then overlaps UDP CMD_BUFFER, and
; APPEND_IX_STR(HOST_BUFF) inside UDP.OPEN_LOCAL falls into an infinite
; loop reading from the buffer it's currently writing to (the bug that
; corrupted memory with repeated "AT+CIPSTART=\"UDP\",\"192.168.1." pattern).
TFTP_BSS_BASE	EQU UDP.UDP_BSS_END
NUM_PRINT_BUFF	EQU TFTP_BSS_BASE
ARG1_BUFF	EQU NUM_PRINT_BUFF + 8
ARG2_BUFF	EQU ARG1_BUFF + URL_SIZE
ARG3_BUFF	EQU ARG2_BUFF + URL_SIZE
URL_BUFF	EQU ARG3_BUFF + URL_SIZE
HOST_BUFF	EQU URL_BUFF + URL_SIZE
PORT_BUFF	EQU HOST_BUFF + HOST_SIZE
REMOTE_FILE	EQU PORT_BUFF + PORT_SIZE
OUT_FILE	EQU REMOTE_FILE + REMOTE_FILE_SIZE
PACKET_BUFFER	EQU OUT_FILE + LOCAL_FILE_SIZE
ACK_BUFFER	EQU PACKET_BUFFER + TFTP_PACKET_SIZE
TFTP_BSS_END	EQU ACK_BUFFER + 4
	; PACKET_BUFFER is the DSS_WRITE source on download and DSS_READ_FILE
	; target on upload. Keep it in PAGE2 (after code+BSS) until PAGE1
	; safety across DSS calls is confirmed; then it can move to BIGBUF.
	ASSERT	TFTP_BSS_END < 0xC000

