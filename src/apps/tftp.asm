; ======================================================
; TFTP client for Sprinter DSS Network Kit
; Minimal RFC 1350 RRQ downloader over ESP-AT UDP.
; ======================================================

EXE_VERSION		EQU 1
DEFAULT_TIMEOUT		EQU 2000
RECV_TIMEOUT		EQU 5000
RETRY_LIMIT		EQU 3
URL_SIZE		EQU 160
HOST_SIZE		EQU 96
PORT_SIZE		EQU 8
REMOTE_FILE_SIZE	EQU 128
LOCAL_FILE_SIZE	EQU 80
TFTP_PACKET_SIZE	EQU 516
TFTP_DATA_SIZE		EQU 512
DSS_CREATE_OVERWRITE	EQU 0x0A
NO_HANDLE		EQU 0xFF

	DEVICE NOSLOT64K

	INCLUDE "macro.inc"
	INCLUDE "dss.inc"

	MODULE MAIN

	ORG 0x8080

EXE_HEADER
	DB "EXE"
	DB EXE_VERSION
	DW 0x0080
	DW 0
	DW 0
	DW 0
	DW 0
	DW 0
	DW START
	DW START
	DW STACK_TOP
	DS 106, 0

	ORG 0x8100
@STACK_TOP

START
	CALL	CLEAR_BSS
	LD	A,NO_HANDLE
	LD	(OUT_FH),A
	XOR	A
	LD	(UDP_IS_OPEN),A

	CALL	ISA.ISA_RESET
	CALL	WCOMMON.INIT_VMODE
	PRINTLN MSG_START

	CALL	PARSE_CMD_LINE
	JP	C,USAGE
	CALL	PARSE_URL
	JP	C,USAGE
	CALL	PRINT_SOURCE_INFO

	CALL	OPEN_OUTPUT_FILE
	JP	C,FILE_ERROR_EXIT

	CALL	WIFI.UART_FIND
	JP	C,NO_WIFI

	CALL	NETCFG.LOAD
	CALL	NETCFG.APPLY_UART_BAUD
	CALL	WIFI.UART_INIT
	PRINTLN MSG_UART_READY

	LD	HL,CMD_AT
	CALL	SEND_CMD_RECOVER

	LD	HL,CMD_ECHO_OFF
	CALL	SEND_CMD

	LD	HL,CMD_CIPMUX_0
	CALL	SEND_CMD

	PRINT MSG_CONNECTING
	PRINT HOST_BUFF
	PRINT MSG_COLON
	PRINT PORT_BUFF
	PRINT WCOMMON.LINE_END
	LD	HL,HOST_BUFF
	LD	DE,PORT_BUFF
	LD	IX,LOCAL_PORT
	CALL	UDP.OPEN_LOCAL
	JP	C,UDP_ERROR_EXIT
	LD	A,1
	LD	(UDP_IS_OPEN),A
	CALL	VERIFY_UDP_OPEN
	JP	C,UDP_ERROR_EXIT

	CALL	DOWNLOAD_RRQ
	JP	C,UDP_ERROR_EXIT

	CALL	UDP.CLOSE
	XOR	A
	LD	(UDP_IS_OPEN),A
	CALL	CLOSE_OUTPUT_FILE
	JP	C,FILE_ERROR_EXIT

	PRINT WCOMMON.LINE_END
	PRINTLN MSG_DONE
	LD	B,0
	JP	WCOMMON.EXIT

NO_WIFI
	CALL	CLOSE_OUTPUT_FILE
	PRINTLN MSG_WIFI_NOT_FOUND
	LD	B,2
	JP	WCOMMON.EXIT

USAGE
	PRINTLN MSG_USAGE
	LD	B,1
	JP	WCOMMON.EXIT

UDP_ERROR_EXIT
	PUSH	AF
	LD	A,(UDP_IS_OPEN)
	AND	A
	CALL	NZ,UDP.CLOSE
	XOR	A
	LD	(UDP_IS_OPEN),A
	CALL	CLOSE_OUTPUT_FILE
	POP	AF
	ADD	A,'0'
	LD	(MSG_ERROR_NO),A
	PRINTLN MSG_NET_ERROR
	LD	B,3
	JP	WCOMMON.EXIT

FILE_ERROR_EXIT
	CALL	PRINT_FILE_ERROR
	LD	B,5
	JP	WCOMMON.EXIT

; ------------------------------------------------------
; Parse command line: TFTP.EXE tftp://host[:port]/path FILE
; ------------------------------------------------------
PARSE_CMD_LINE
	LD	HL,0x8080
	LD	A,(HL)
	AND	A
	JR	Z,.ERR
	LD	B,A
	INC	HL
	CALL	SKIP_SPACES
	JR	C,.ERR
	LD	DE,URL_BUFF
	LD	C,URL_SIZE-1
	CALL	COPY_ARG
	JR	C,.ERR
	CALL	SKIP_SPACES
	JR	C,.ERR
	LD	DE,OUT_FILE
	LD	C,LOCAL_FILE_SIZE-1
	CALL	COPY_ARG
	RET	NC
.ERR
	SCF
	RET

SKIP_SPACES
	LD	A,B
	AND	A
	JR	Z,.ERR
	LD	A,(HL)
	CP	0x21
	RET	NC
	INC	HL
	DJNZ	SKIP_SPACES
.ERR
	SCF
	RET

COPY_ARG
	XOR	A
	LD	(ARG_LEN),A
.NEXT
	LD	A,B
	AND	A
	JR	Z,.END
	LD	A,(HL)
	CP	0x21
	JR	C,.END
	LD	A,C
	AND	A
	JR	Z,.ERR
	LD	A,(HL)
	LD	(DE),A
	INC	DE
	INC	HL
	DEC	B
	DEC	C
	LD	A,(ARG_LEN)
	INC	A
	LD	(ARG_LEN),A
	JR	.NEXT
.END
	XOR	A
	LD	(DE),A
	LD	A,(ARG_LEN)
	AND	A
	RET	NZ
.ERR
	SCF
	RET

; ------------------------------------------------------
; Parse URL_BUFF into HOST_BUFF, PORT_BUFF and REMOTE_FILE.
; ------------------------------------------------------
PARSE_URL
	LD	HL,URL_BUFF
	LD	DE,TFTP_SCHEME
	CALL	UTIL.STARTSWITH
	JR	NZ,.ERR
	LD	DE,7
	ADD	HL,DE

	LD	DE,HOST_BUFF
	LD	B,HOST_SIZE-1
.HOST
	LD	A,(HL)
	AND	A
	JR	Z,.ERR
	CP	'/'
	JR	Z,.HOST_DONE
	CP	':'
	JR	Z,.PORT
	DJNZ	.HOST_STORE
	JR	.ERR
.HOST_STORE
	LD	(DE),A
	INC	DE
	INC	HL
	JR	.HOST
.HOST_DONE
	XOR	A
	LD	(DE),A
	LD	DE,DEFAULT_PORT
	LD	HL,PORT_BUFF
	CALL	COPY_ASCIIZ_HL
	LD	HL,URL_BUFF
	LD	DE,TFTP_SCHEME
	CALL	UTIL.STARTSWITH
	LD	DE,7
	ADD	HL,DE
.SKIP_TO_PATH
	LD	A,(HL)
	CP	'/'
	JR	Z,.PATH
	AND	A
	JR	Z,.ERR
	INC	HL
	JR	.SKIP_TO_PATH
.PORT
	XOR	A
	LD	(DE),A
	INC	HL
	LD	DE,PORT_BUFF
	LD	B,PORT_SIZE-1
.PORT_LOOP
	LD	A,(HL)
	AND	A
	JR	Z,.ERR
	CP	'/'
	JR	Z,.PATH
	CP	'0'
	JR	C,.ERR
	CP	'9'+1
	JR	NC,.ERR
	DJNZ	.PORT_STORE
	JR	.ERR
.PORT_STORE
	LD	(DE),A
	INC	DE
	INC	HL
	JR	.PORT_LOOP
.PATH
	XOR	A
	LD	(DE),A
	INC	HL
	LD	A,(HL)
	AND	A
	JR	Z,.ERR
	LD	DE,REMOTE_FILE
	LD	B,REMOTE_FILE_SIZE-1
.PATH_LOOP
	LD	A,(HL)
	AND	A
	JR	Z,.PATH_DONE
	DJNZ	.PATH_STORE
	JR	.ERR
.PATH_STORE
	LD	(DE),A
	INC	DE
	INC	HL
	JR	.PATH_LOOP
.PATH_DONE
	XOR	A
	LD	(DE),A
	AND	A
	RET
.ERR
	SCF
	RET

COPY_ASCIIZ_HL
	LD	A,(DE)
	LD	(HL),A
	AND	A
	RET	Z
	INC	HL
	INC	DE
	JR	COPY_ASCIIZ_HL

; ------------------------------------------------------
; TFTP RRQ download.
; ------------------------------------------------------
DOWNLOAD_RRQ
	LD	HL,1
	LD	(EXPECTED_BLOCK),HL
	CALL	BUILD_RRQ_PACKET
	CALL	SEND_PACKET
	RET	C

.RECV_NEXT
	CALL	RECEIVE_PACKET_RETRY
	RET	C
	CALL	HANDLE_PACKET
	RET	C
	LD	A,(TRANSFER_DONE)
	AND	A
	JR	Z,.RECV_NEXT
	XOR	A
	RET

BUILD_RRQ_PACKET
	LD	HL,PACKET_BUFFER
	XOR	A
	LD	(HL),A
	INC	HL
	LD	(HL),1
	INC	HL
	LD	DE,REMOTE_FILE
	CALL	APPEND_STR_TO_HL
	INC	HL
	LD	DE,MODE_OCTET
	CALL	APPEND_STR_TO_HL
	INC	HL
	LD	DE,PACKET_BUFFER
	AND	A
	SBC	HL,DE
	LD	BC,HL
	LD	(LAST_TX_LEN),BC
	LD	HL,PACKET_BUFFER
	LD	(LAST_TX_PTR),HL
	RET

BUILD_ACK_PACKET
	LD	HL,ACK_BUFFER
	XOR	A
	LD	(HL),A
	INC	HL
	LD	(HL),4
	INC	HL
	LD	A,(EXPECTED_BLOCK+1)
	LD	(HL),A
	INC	HL
	LD	A,(EXPECTED_BLOCK)
	LD	(HL),A
	LD	HL,ACK_BUFFER
	LD	(LAST_TX_PTR),HL
	LD	BC,4
	LD	(LAST_TX_LEN),BC
	RET

SEND_PACKET
	LD	HL,(LAST_TX_PTR)
	LD	BC,(LAST_TX_LEN)
	JP	UDP.SEND_BUFFER

RECEIVE_PACKET_RETRY
	LD	A,RETRY_LIMIT
	LD	(RETRIES_LEFT),A
.TRY
	LD	HL,PACKET_BUFFER
	LD	BC,TFTP_PACKET_SIZE
	LD	DE,RECV_TIMEOUT
	CALL	UDP.RECEIVE
	RET	NC
	CP	RES_RS_TIMEOUT
	RET	NZ
	LD	HL,RETRIES_LEFT
	DEC	(HL)
	JR	Z,.TIMEOUT
	PRINTLN MSG_RETRY
	CALL	SEND_PACKET
	RET	C
	JR	.TRY
.TIMEOUT
	LD	A,RES_RS_TIMEOUT
	SCF
	RET

HANDLE_PACKET
	LD	(RECV_LEN),BC
	LD	A,B
	OR	C
	JR	Z,.PROTO_ERR
	LD	HL,(RECV_LEN)
	LD	DE,4
	CALL	HL_LT_DE
	JR	C,.PROTO_ERR

	LD	HL,PACKET_BUFFER
	LD	A,(HL)
	AND	A
	JR	NZ,.PROTO_ERR
	INC	HL
	LD	A,(HL)
	CP	3
	JR	Z,.DATA
	CP	5
	JR	Z,.TFTP_ERROR
	JR	.PROTO_ERR

.DATA
	INC	HL
	LD	A,(EXPECTED_BLOCK+1)
	CP	(HL)
	JR	NZ,.PROTO_ERR
	INC	HL
	LD	A,(EXPECTED_BLOCK)
	CP	(HL)
	JR	NZ,.PROTO_ERR

	CALL	WRITE_DATA_PAYLOAD
	RET	C
	CALL	BUILD_ACK_PACKET
	CALL	SEND_PACKET
	RET	C
	CALL	CHECK_FINAL_BLOCK
	CALL	INC_EXPECTED_BLOCK
	XOR	A
	RET

.TFTP_ERROR
	CALL	PRINT_TFTP_ERROR
	LD	A,RES_ERROR
	SCF
	RET
.PROTO_ERR
	PRINTLN MSG_PROTO_ERROR
	LD	A,RES_ERROR
	SCF
	RET

WRITE_DATA_PAYLOAD
	LD	BC,(RECV_LEN)
	DEC	BC
	DEC	BC
	DEC	BC
	DEC	BC
	LD	A,B
	OR	C
	RET	Z
	LD	HL,PACKET_BUFFER+4
	LD	A,(OUT_FH)
	LD	D,B
	LD	E,C
	LD	C,DSS_WRITE
	RST	DSS
	RET

CHECK_FINAL_BLOCK
	LD	HL,(RECV_LEN)
	LD	DE,TFTP_PACKET_SIZE
	CALL	HL_LT_DE
	JR	NC,.MORE
	LD	A,1
	LD	(TRANSFER_DONE),A
	RET
.MORE
	PRINT MSG_PROGRESS
	RET

INC_EXPECTED_BLOCK
	LD	HL,(EXPECTED_BLOCK)
	INC	HL
	LD	(EXPECTED_BLOCK),HL
	RET

HL_LT_DE
	LD	A,H
	CP	D
	RET	NZ
	LD	A,L
	CP	E
	RET

; ------------------------------------------------------
; File helpers.
; ------------------------------------------------------
OPEN_OUTPUT_FILE
	PRINT MSG_OUTPUT
	PRINT OUT_FILE
	PRINT WCOMMON.LINE_END
	LD	HL,OUT_FILE
	LD	A,FA_ARCHIVE
	LD	C,DSS_CREATE_OVERWRITE
	RST	DSS
	RET	C
	LD	(OUT_FH),A
	RET

CLOSE_OUTPUT_FILE
	LD	A,(OUT_FH)
	CP	NO_HANDLE
	RET	Z
	LD	C,DSS_CLOSE_FILE
	RST	DSS
	LD	A,NO_HANDLE
	LD	(OUT_FH),A
	RET

PRINT_FILE_ERROR
	PRINT	MSG_FILE_ERROR
	CALL	DSS_ERROR.GET_ERR_MSG
	PRINTLN_HL
	RET

; ------------------------------------------------------
; ESP/UDP command helpers.
; ------------------------------------------------------
SEND_CMD
	LD	DE,WIFI.RS_BUFF
	LD	BC,DEFAULT_TIMEOUT
	CALL	WIFI.UART_TX_CMD
	AND	A
	RET	Z
	JP	UDP_ERROR_EXIT

SEND_CMD_RECOVER
	PUSH	HL
	LD	DE,WIFI.RS_BUFF
	LD	BC,DEFAULT_TIMEOUT
	CALL	WIFI.UART_TX_CMD
	AND	A
	JR	Z,.OK

	PRINTLN MSG_RESETTING_ESP
	CALL	WIFI.ESP_RESET
	CALL	WIFI.UART_SET_DEFAULT_DIVISOR
	CALL	WIFI.UART_INIT
	POP	HL
	JP	SEND_CMD
.OK
	POP	HL
	RET

VERIFY_UDP_OPEN
	LD	HL,CMD_CIPSTATUS
	LD	DE,WIFI.RS_BUFF
	LD	BC,DEFAULT_TIMEOUT
	CALL	WIFI.UART_TX_CMD
	AND	A
	JR	NZ,.ERR
	LD	HL,WIFI.RS_BUFF
	LD	DE,UDP_STATUS_TOKEN
	CALL	FIND_SUBSTR
	RET	NC
	PRINTLN MSG_UDP_NOT_CONFIRMED
.ERR
	LD	A,RES_ERROR
	SCF
	RET

FIND_SUBSTR
	LD	A,(DE)
	AND	A
	RET	Z
.NEXT_START
	LD	A,(HL)
	AND	A
	JR	Z,.NOT_FOUND
	PUSH	HL
	PUSH	DE
.MATCH
	LD	A,(DE)
	AND	A
	JR	Z,.FOUND_POP
	LD	B,A
	LD	A,(HL)
	AND	A
	JR	Z,.NO_MATCH_POP
	CP	B
	JR	NZ,.NO_MATCH_POP
	INC	HL
	INC	DE
	JR	.MATCH
.FOUND_POP
	POP	DE
	POP	HL
	XOR	A
	RET
.NO_MATCH_POP
	POP	DE
	POP	HL
	INC	HL
	JR	.NEXT_START
.NOT_FOUND
	SCF
	RET

APPEND_STR_TO_HL
	LD	A,(DE)
	LD	(HL),A
	AND	A
	RET	Z
	INC	HL
	INC	DE
	JR	APPEND_STR_TO_HL

PRINT_SOURCE_INFO
	PRINT MSG_REMOTE
	PRINT URL_BUFF
	PRINT WCOMMON.LINE_END
	PRINT MSG_OUTPUT
	PRINT OUT_FILE
	PRINT WCOMMON.LINE_END
	RET

PRINT_TFTP_ERROR
	PRINT MSG_TFTP_ERROR
	LD	HL,PACKET_BUFFER+4
	LD	B,80
.NEXT
	LD	A,(HL)
	AND	A
	JR	Z,.DONE
	LD	C,DSS_PUTCHAR
	RST	DSS
	INC	HL
	DJNZ	.NEXT
.DONE
	PRINT WCOMMON.LINE_END
	RET

CLEAR_BSS
	LD	HL,TFTP_BSS_BASE
	LD	DE,TFTP_BSS_BASE+1
	LD	BC,TFTP_BSS_END-TFTP_BSS_BASE-1
	LD	(HL),0
	LDIR
	RET

MSG_START
	DB "TFTP - UDP TFTP downloader for SprinterESP",0
MSG_USAGE
	DB "Usage: TFTP.EXE tftp://host[:port]/path FILE",0
MSG_REMOTE
	DB "Source URL: ",0
MSG_OUTPUT
	DB "Output file: ",0
MSG_UART_READY
	DB "UART initialized.",0
MSG_RESETTING_ESP
	DB "ESP did not answer, resetting module.",0
MSG_CONNECTING
	DB "Opening UDP endpoint to ",0
MSG_COLON
	DB ":",0
MSG_RETRY
	DB "Timeout, retrying.",0
MSG_PROGRESS
	DB ".",0
MSG_DONE
	DB "TFTP done.",0
MSG_WIFI_NOT_FOUND
	DB "Sprinter-WiFi not found!",0
MSG_FILE_ERROR
	DB "File error: ",0
MSG_NET_ERROR
	DB "Network/ESP error #"
MSG_ERROR_NO
	DB "n!",0
MSG_PROTO_ERROR
	DB "Unexpected TFTP packet.",0
MSG_TFTP_ERROR
	DB "TFTP error: ",0
MSG_UDP_NOT_CONFIRMED
	DB "UDP socket was not confirmed by ESP status.",0

CMD_AT
	DB "AT",13,10,0
CMD_ECHO_OFF
	DB "ATE0",13,10,0
CMD_CIPMUX_0
	DB "AT+CIPMUX=0",13,10,0
CMD_CIPSTATUS
	DB "AT+CIPSTATUS",13,10,0
UDP_STATUS_TOKEN
	DB "UDP",0

TFTP_SCHEME
	DB "tftp://",0
DEFAULT_PORT
	DB "69",0
LOCAL_PORT
	DB "1069",0
MODE_OCTET
	DB "octet",0

ARG_LEN		DB 0
OUT_FH		DB NO_HANDLE
UDP_IS_OPEN	DB 0
TRANSFER_DONE	DB 0
RETRIES_LEFT	DB 0
EXPECTED_BLOCK	DW 1
RECV_LEN	DW 0
LAST_TX_PTR	DW 0
LAST_TX_LEN	DW 0

URL_BUFF
	DS URL_SIZE,0
HOST_BUFF
	DS HOST_SIZE,0
PORT_BUFF
	DS PORT_SIZE,0
REMOTE_FILE
	DS REMOTE_FILE_SIZE,0
OUT_FILE
	DS LOCAL_FILE_SIZE,0

	ENDMODULE

	DEFINE ESP_TCP_BSS_BASE_OVERRIDE
ESP_TCP_BSS_BASE	EQU 0xB000

	INCLUDE "wcommon.asm"
	INCLUDE "dss_error.asm"
	INCLUDE "isa.asm"
	INCLUDE "netcfg_lib.asm"
	INCLUDE "esp_tcp.asm"
	INCLUDE "esp_udp.asm"
	INCLUDE "esplib.asm"

TFTP_BSS_BASE	EQU UDP.UDP_BSS_END
PACKET_BUFFER	EQU TFTP_BSS_BASE
ACK_BUFFER	EQU PACKET_BUFFER + TFTP_PACKET_SIZE
TFTP_BSS_END	EQU ACK_BUFFER + 4

	END MAIN.START
