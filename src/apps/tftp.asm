; ------------------------------------------------------
; TFTP RRQ download.
; ------------------------------------------------------
TRANSFER_TFTP
	LD	A,(TRANSFER_MODE)
	AND	A
	JP	NZ,UPLOAD_WRQ

DOWNLOAD_RRQ
	LD	HL,1
	LD	(EXPECTED_BLOCK),HL
	CALL	BUILD_RRQ_PACKET
	CALL	SEND_PACKET
	RET	C
	LD	A,RETRY_LIMIT
	LD	(HANDLE_RETRIES),A

.RECV_NEXT
	CALL	WCOMMON.CHECK_CANCEL
	JP	C,CANCEL_EXIT
	; Each TFTP DATA is its own UDP datagram. If a previous READ_PAYLOAD
	; timed out mid-datagram, PAYLOAD_LEFT is still non-zero, and the next
	; RECEIVE would skip WAIT_IPD_HEADER and start eating the +IPD header
	; of the retransmitted block as if it were data. Force a fresh +IPD
	; parse on every attempt.
	LD	HL,0
	LD	(TCP.PAYLOAD_LEFT),HL
	XOR	A
	LD	(TCP.LSR_ACCUM),A			; clear sticky LSR error bits
	CALL	RECEIVE_PACKET_RETRY
	JR	C,.RX_TIMEOUT
	; UART overrun (or other LSR error) detected during this RECEIVE
	; means the +IPD payload is misaligned — even a "correct"-looking
	; TFTP header at byte 0 is just a coincidence of corrupted bytes.
	; Drain the UART, scrub state and force the same retry path as a
	; protocol error (server retransmits DATA after no-ACK timeout).
	LD	A,(TCP.LSR_ACCUM)
	AND	LSR_OE | LSR_PE | LSR_FE | LSR_BI | LSR_RCVE
	JR	NZ,.RX_CORRUPT
	CALL	HANDLE_PACKET
	JR	C,.HANDLE_FAIL
	; Reset the corruption-retry counter on every successful packet —
	; transient UART OE / ESP desync should not accumulate against later
	; blocks.
	LD	A,RETRY_LIMIT
	LD	(HANDLE_RETRIES),A
	LD	A,(TRANSFER_DONE)
	AND	A
	JR	Z,.RECV_NEXT
	XOR	A
	RET

.RX_TIMEOUT
	; Receive returned an error. If LSR_ACCUM has any error bit set,
	; the failure was caused by OE/PE/FE corrupting the +IPD parser
	; mid-frame (the parser can return RES_RS_TIMEOUT *or* RES_ERROR
	; depending on where the corruption struck the length field) —
	; treat both as recoverable: DRAIN + ACK retransmit + retry. Without
	; this, OE that bubbles out as #1 (RES_ERROR from
	; READ_IPD_LEN.ERROR_BAD_CHAR) or #4 (RES_RS_TIMEOUT from a stuck
	; payload read) silently aborts a transfer that should retry.
	LD	C,A
	LD	A,(TCP.LSR_ACCUM)
	AND	LSR_OE | LSR_PE | LSR_FE | LSR_BI | LSR_RCVE
	JR	NZ,.RX_CORRUPT
	; Pure receive timeout (no data, no UART error). Propagate caller's
	; result code unchanged.
	LD	A,C
	SCF
	RET

.RX_CORRUPT
	; UART overrun: previous packet was lost / mis-aligned. Drain UART
	; state and re-send our last ACK *immediately* — without it, the
	; server's own retransmit timer (1–5 s) may be the only thing nudging
	; the next DATA, and if the server already retried during our drain
	; window it can give up before our next RECV_TIMEOUT (5 s) elapses.
	; The "Sorcerer's Apprentice" concern (RFC 1350) only matters when
	; we are exchanging ACKs for blocks the server has already seen ACKed;
	; here we never delivered the DATA, so an extra ACK for the previous
	; block is exactly what TFTP recovery expects.
	CALL	DRAIN_UART_AFTER_OE
	PRINTLN MSG_OVERRUN
	CALL	SEND_PACKET
	LD	HL,HANDLE_RETRIES
	DEC	(HL)
	JR	Z,.OE_GIVEUP
	JR	.RECV_NEXT
.OE_GIVEUP
	LD	A,RES_RS_TIMEOUT
	SCF
	RET

.HANDLE_FAIL
	; Server-side TFTP ERROR (opcode 5) is fatal — propagate immediately.
	; Anything else (PROTO_ERR — header that *parsed* but didn't match
	; expected block) is recoverable: resend our last ACK to nudge the
	; server's retransmit logic, since its own timer might be far away.
	CP	RES_ERROR
	RET	Z
	PUSH	AF
	LD	HL,HANDLE_RETRIES
	DEC	(HL)
	JR	Z,.HANDLE_GIVEUP
	POP	AF
	PRINTLN MSG_RETRY
	CALL	SEND_PACKET
	RET	C
	JR	.RECV_NEXT
.HANDLE_GIVEUP
	POP	AF
	SCF
	RET

BUILD_RRQ_PACKET
	LD	HL,PACKET_BUFFER
	XOR	A
	LD	(HL),A
	INC	HL
	LD	(HL),1
	INC	HL
	LD	DE,REMOTE_FILE
	CALL	APPEND_STR_TO_HL
	INC	HL
	LD	DE,MODE_OCTET
	CALL	APPEND_STR_TO_HL
	INC	HL
	LD	DE,PACKET_BUFFER
	AND	A
	SBC	HL,DE
	LD	BC,HL
	LD	(LAST_TX_LEN),BC
	LD	HL,PACKET_BUFFER
	LD	(LAST_TX_PTR),HL
	RET

; ------------------------------------------------------
; TFTP WRQ upload.
; ------------------------------------------------------
UPLOAD_WRQ
	LD	HL,0
	LD	(EXPECTED_BLOCK),HL
	CALL	BUILD_WRQ_PACKET
	CALL	SEND_PACKET
	RET	C
	LD	A,RETRY_LIMIT
	LD	(HANDLE_RETRIES),A
	LD	HL,0
	LD	(TCP.PAYLOAD_LEFT),HL
	CALL	WAIT_ACK_WITH_RETRY
	RET	C

	LD	HL,1
	LD	(EXPECTED_BLOCK),HL
.NEXT_BLOCK
	CALL	WCOMMON.CHECK_CANCEL
	JP	C,CANCEL_EXIT
	CALL	BUILD_DATA_PACKET_FROM_FILE
	RET	C
	CALL	SEND_PACKET
	RET	C
	LD	A,RETRY_LIMIT
	LD	(HANDLE_RETRIES),A
	; See note in DOWNLOAD_RRQ — clear stale PAYLOAD_LEFT before each ACK
	; receive so a previous mid-packet timeout doesn't desync the next
	; +IPD header parse.
	LD	HL,0
	LD	(TCP.PAYLOAD_LEFT),HL
	CALL	WAIT_ACK_WITH_RETRY
	RET	C
	CALL	CHECK_UPLOAD_DONE
	RET	Z
	CALL	INC_EXPECTED_BLOCK
	JR	.NEXT_BLOCK

BUILD_WRQ_PACKET
	LD	HL,PACKET_BUFFER
	XOR	A
	LD	(HL),A
	INC	HL
	LD	(HL),2
	INC	HL
	LD	DE,REMOTE_FILE
	CALL	APPEND_STR_TO_HL
	INC	HL
	LD	DE,MODE_OCTET
	CALL	APPEND_STR_TO_HL
	INC	HL
	LD	DE,PACKET_BUFFER
	AND	A
	SBC	HL,DE
	LD	BC,HL
	LD	(LAST_TX_LEN),BC
	LD	HL,PACKET_BUFFER
	LD	(LAST_TX_PTR),HL
	RET

BUILD_DATA_PACKET_FROM_FILE
	LD	HL,PACKET_BUFFER
	XOR	A
	LD	(HL),A
	INC	HL
	LD	(HL),3
	INC	HL
	LD	A,(EXPECTED_BLOCK+1)
	LD	(HL),A
	INC	HL
	LD	A,(EXPECTED_BLOCK)
	LD	(HL),A

	; Pause RX during the slow DSS_READ_FILE; resume so SEND_PACKET that
	; follows can read its '>' / SEND OK via WAIT_PROMPT.
	CALL	WIFI.UART_RX_PAUSE
	LD	A,(IN_FH)
	LD	HL,PACKET_BUFFER+4
	LD	DE,TFTP_DATA_SIZE
	LD	C,DSS_READ_FILE
	RST	DSS
	PUSH	AF,DE
	CALL	WIFI.UART_RX_RESUME
	POP	DE,AF
	JR	C,.FILE_ERROR
	LD	(LAST_DATA_LEN),DE
	LD	BC,DE
	INC	BC
	INC	BC
	INC	BC
	INC	BC
	LD	(LAST_TX_LEN),BC
	LD	HL,PACKET_BUFFER
	LD	(LAST_TX_PTR),HL
	XOR	A
	RET
.FILE_ERROR
	LD	(FILE_ERROR_CODE),A
	LD	A,1
	LD	(FILE_ERROR_FLAG),A
	SCF
	RET

; Wrap RECEIVE_ACK_RETRY with corruption-retry. Caller must initialise
; HANDLE_RETRIES before invoking. On a PROTO_ERR (RES_RS_TIMEOUT from
; HANDLE_ACK_PACKET) we resend the last packet and try again, until the
; corruption budget is exhausted or a fatal/server error occurs.
WAIT_ACK_WITH_RETRY
	; Reset stale PAYLOAD_LEFT (see DOWNLOAD_RRQ note).
	LD	HL,0
	LD	(TCP.PAYLOAD_LEFT),HL
	XOR	A
	LD	(TCP.LSR_ACCUM),A
	CALL	RECEIVE_ACK_RETRY
	JR	C,.RECV_FAIL
	; Same OE check as in download — corrupt UART means the +IPD parser
	; landed on misaligned bytes, so even a "correct"-looking ACK is just
	; a coincidence. Drain and retry.
	LD	A,(TCP.LSR_ACCUM)
	AND	LSR_OE | LSR_PE | LSR_FE | LSR_BI | LSR_RCVE
	JR	NZ,.RX_CORRUPT
	XOR	A
	RET

.RECV_FAIL
	; If a UART error bit is set, the receive failure was caused by OE
	; corrupting the +IPD parser — recover via DRAIN + retransmit nudge.
	; Preserve A (RECEIVE_ACK_RETRY's result) across the LSR check so
	; non-OE error codes still propagate via the normal path below.
	LD	C,A
	LD	A,(TCP.LSR_ACCUM)
	AND	LSR_OE | LSR_PE | LSR_FE | LSR_BI | LSR_RCVE
	JR	NZ,.RX_CORRUPT
	LD	A,C
	CP	RES_RS_TIMEOUT
	RET	NZ							; not a recoverable case
	JR	.RETRY

.RX_CORRUPT
	; OE on ACK reception. Resend our last DATA packet so the server,
	; which may be waiting for our ACK and hold off retransmit, sees
	; activity and either ACKs again or re-sends. Same rationale as
	; DOWNLOAD_RRQ.RX_CORRUPT.
	CALL	DRAIN_UART_AFTER_OE
	PRINTLN MSG_OVERRUN
	CALL	SEND_PACKET
	LD	HL,HANDLE_RETRIES
	DEC	(HL)
	JR	Z,.GIVEUP
	JR	WAIT_ACK_WITH_RETRY
.RETRY
	LD	HL,HANDLE_RETRIES
	DEC	(HL)
	JR	Z,.GIVEUP
	PRINTLN MSG_RETRY
	CALL	SEND_PACKET
	RET	C
	JR	WAIT_ACK_WITH_RETRY
.GIVEUP
	LD	A,RES_RS_TIMEOUT
	SCF
	RET

RECEIVE_ACK_RETRY
	LD	A,RETRY_LIMIT
	LD	(RETRIES_LEFT),A
.TRY
	; Force fresh +IPD parse on every attempt — see DOWNLOAD_RRQ note.
	LD	HL,0
	LD	(TCP.PAYLOAD_LEFT),HL
	LD	HL,PACKET_BUFFER
	LD	BC,TFTP_PACKET_SIZE
	LD	DE,RECV_TIMEOUT
	CALL	UDP.RECEIVE
	JR	NC,.GOT_PACKET
	CP	RES_RS_TIMEOUT
	RET	NZ
	LD	HL,RETRIES_LEFT
	DEC	(HL)
	JR	Z,.TIMEOUT
	PRINTLN MSG_RETRY
	CALL	SEND_PACKET
	RET	C
	JR	.TRY
.TIMEOUT
	LD	A,RES_RS_TIMEOUT
	SCF
	RET
.GOT_PACKET
	CALL	HANDLE_ACK_PACKET
	RET

HANDLE_ACK_PACKET
	LD	(RECV_LEN),BC
	LD	HL,(RECV_LEN)
	LD	DE,4
	CALL	HL_LT_DE
	JR	C,.PROTO_ERR
	LD	HL,PACKET_BUFFER
	LD	A,(HL)
	AND	A
	JR	NZ,.PROTO_ERR
	INC	HL
	LD	A,(HL)
	CP	4
	JR	Z,.ACK
	CP	5
	JR	Z,.TFTP_ERROR
	JR	.PROTO_ERR
.ACK
	INC	HL
	LD	A,(EXPECTED_BLOCK+1)
	CP	(HL)
	JR	NZ,.PROTO_ERR
	INC	HL
	LD	A,(EXPECTED_BLOCK)
	CP	(HL)
	JR	NZ,.PROTO_ERR
	XOR	A
	RET
.TFTP_ERROR
	CALL	PRINT_TFTP_ERROR
	LD	A,RES_ERROR
	SCF
	RET
.PROTO_ERR
	; See note in HANDLE_PACKET.PROTO_ERR — RES_RS_TIMEOUT marks this
	; failure as recoverable (corrupt ACK, retransmit DATA may help).
	PRINTLN MSG_PROTO_ERROR
	LD	A,RES_RS_TIMEOUT
	SCF
	RET

CHECK_UPLOAD_DONE
	LD	HL,(LAST_DATA_LEN)
	LD	DE,TFTP_DATA_SIZE
	CALL	HL_LT_DE
	JR	C,.DONE
	PRINT MSG_PROGRESS
	OR	1
	RET
.DONE
	XOR	A
	RET

BUILD_ACK_PACKET
	LD	HL,ACK_BUFFER
	XOR	A
	LD	(HL),A
	INC	HL
	LD	(HL),4
	INC	HL
	LD	A,(EXPECTED_BLOCK+1)
	LD	(HL),A
	INC	HL
	LD	A,(EXPECTED_BLOCK)
	LD	(HL),A
	LD	HL,ACK_BUFFER
	LD	(LAST_TX_PTR),HL
	LD	BC,4
	LD	(LAST_TX_LEN),BC
	RET

SEND_PACKET
	LD	HL,(LAST_TX_PTR)
	LD	BC,(LAST_TX_LEN)
	JP	UDP.SEND_BUFFER

RECEIVE_PACKET_RETRY
	LD	A,RETRY_LIMIT
	LD	(RETRIES_LEFT),A
.TRY
	; Force fresh +IPD parse on every attempt — see DOWNLOAD_RRQ note.
	LD	HL,0
	LD	(TCP.PAYLOAD_LEFT),HL
	LD	HL,PACKET_BUFFER
	LD	BC,TFTP_PACKET_SIZE
	LD	DE,RECV_TIMEOUT
	CALL	UDP.RECEIVE
	RET	NC
	CP	RES_RS_TIMEOUT
	RET	NZ
	LD	HL,RETRIES_LEFT
	DEC	(HL)
	JR	Z,.TIMEOUT
	PRINTLN MSG_RETRY
	CALL	SEND_PACKET
	RET	C
	JR	.TRY
.TIMEOUT
	LD	A,RES_RS_TIMEOUT
	SCF
	RET

; ------------------------------------------------------
; Recover from a UART overrun: deassert RTS, give ESP up to ~50 ms to
; drain any bytes still queued in its UART TX buffer, reset the 16550 RX
; FIFO and clear the sticky LSR_ACCUM. RX is resumed on exit so the
; subsequent retry path (PRINTLN "UART overrun, retrying" + next
; RECEIVE) and any intermediate SEND_PACKET work normally.
; ------------------------------------------------------
DRAIN_UART_AFTER_OE
	PUSH	AF,BC,DE,HL
	CALL	@WIFI.UART_RX_PAUSE
	LD	HL,50
	CALL	@UTIL.DELAY
	CALL	@WIFI.UART_EMPTY_RS
	XOR	A
	LD	(TCP.LSR_ACCUM),A
	LD	HL,0
	LD	(TCP.PAYLOAD_LEFT),HL
	LD	(TCP.LAST_IPD_LEN),HL
	CALL	@WIFI.UART_RX_RESUME
	POP	HL,DE,BC,AF
	RET

HANDLE_PACKET
	LD	(RECV_LEN),BC
	LD	A,B
	OR	C
	JR	Z,.PROTO_ERR
	LD	HL,(RECV_LEN)
	LD	DE,4
	CALL	HL_LT_DE
	JR	C,.PROTO_ERR

	LD	HL,PACKET_BUFFER
	LD	A,(HL)
	AND	A
	JR	NZ,.PROTO_ERR
	INC	HL
	LD	A,(HL)
	CP	3
	JR	Z,.DATA
	CP	5
	JR	Z,.TFTP_ERROR
	JR	.PROTO_ERR

.DATA
	INC	HL
	LD	A,(EXPECTED_BLOCK+1)
	CP	(HL)
	JR	NZ,.PROTO_ERR
	INC	HL
	LD	A,(EXPECTED_BLOCK)
	CP	(HL)
	JR	NZ,.PROTO_ERR

	; Pause RX during the slow DSS_WRITE so ESP can't push next-block
	; bytes into a 16-byte FIFO we aren't draining. RESUME before ACK
	; build/send so SEND_BUFFER's WAIT_PROMPT can read the '>' / SEND OK
	; responses.
	CALL	WIFI.UART_RX_PAUSE
	CALL	WRITE_DATA_PAYLOAD
	PUSH	AF
	CALL	WIFI.UART_RX_RESUME
	POP	AF
	RET	C
	CALL	BUILD_ACK_PACKET
	CALL	SEND_PACKET
	RET	C
	CALL	CHECK_FINAL_BLOCK
	CALL	INC_EXPECTED_BLOCK
	XOR	A
	RET

.TFTP_ERROR
	CALL	PRINT_TFTP_ERROR
	LD	A,RES_ERROR
	SCF
	RET
.PROTO_ERR
	; Use RES_RS_TIMEOUT (not RES_ERROR) so the caller's recovery path
	; can distinguish a corrupt/desynced packet (retransmit may help)
	; from a server-issued TFTP ERROR (fatal).
	PRINTLN MSG_PROTO_ERROR
	LD	A,RES_RS_TIMEOUT
	SCF
	RET

WRITE_DATA_PAYLOAD
	LD	BC,(RECV_LEN)
	DEC	BC
	DEC	BC
	DEC	BC
	DEC	BC
	LD	A,B
	OR	C
	RET	Z
	LD	HL,PACKET_BUFFER+4
	LD	A,(OUT_FH)
	LD	D,B
	LD	E,C
	LD	C,DSS_WRITE
	RST	DSS
	RET

CHECK_FINAL_BLOCK
	LD	HL,(RECV_LEN)
	LD	DE,TFTP_PACKET_SIZE
	CALL	HL_LT_DE
	JR	NC,.MORE
	LD	A,1
	LD	(TRANSFER_DONE),A
	RET
.MORE
	PRINT MSG_PROGRESS
	RET

INC_EXPECTED_BLOCK
	LD	HL,(EXPECTED_BLOCK)
	INC	HL
	LD	(EXPECTED_BLOCK),HL
	RET

HL_LT_DE
	LD	A,H
	CP	D
	RET	NZ
	LD	A,L
	CP	E
	RET

