; ------------------------------------------------------
; Parse command line:
;   TFTP.EXE host[:port] GET remote-file [-o local-name] [-y]
;   TFTP.EXE host[:port] PUT local-file  [-o remote-name]
; ------------------------------------------------------
PARSE_CMD_LINE
	XOR	A
	LD	(TRANSFER_MODE),A
	LD	(HELP_REQUESTED),A
	LD	(FORCE_OVERWRITE),A
	LD	DE,DEFAULT_PORT
	LD	HL,PORT_BUFF
	CALL	COPY_ASCIIZ_HL
	LD	HL,CMDLINE_ADDR
	LD	A,(HL)
	AND	A
	JP	Z,.ERR
	LD	B,A
	INC	HL
	CALL	SKIP_SPACES
	JP	C,.ERR
	LD	DE,HOST_BUFF
	LD	C,HOST_SIZE-1
	CALL	COPY_ARG
	JP	C,.ERR
	PUSH	HL
	PUSH	BC
	CALL	IS_HOST_HELP
	POP	BC
	POP	HL
	JP	NC,.HELP
	PUSH	HL
	PUSH	BC
	CALL	SPLIT_HOST_PORT
	POP	BC
	POP	HL
	JP	C,.ERR
	LD	A,(HOST_BUFF)
	AND	A
	JP	Z,.ERR

	CALL	SKIP_SPACES
	JP	C,.ERR
	LD	DE,ARG1_BUFF
	LD	C,URL_SIZE-1
	CALL	COPY_ARG
	JP	C,.ERR
	PUSH	HL
	PUSH	BC
	CALL	IS_ARG1_GET
	POP	BC
	POP	HL
	JP	NC,.GET
	PUSH	HL
	PUSH	BC
	CALL	IS_ARG1_PUT
	POP	BC
	POP	HL
	JP	NC,.PUT
	JP	.ERR

.GET
	XOR	A
	LD	(TRANSFER_MODE),A
	CALL	SKIP_SPACES
	JP	C,.ERR
	LD	DE,REMOTE_FILE
	LD	C,REMOTE_FILE_SIZE-1
	CALL	COPY_ARG
	JP	C,.ERR
	JR	.OPTIONS

.PUT
	LD	A,1
	LD	(TRANSFER_MODE),A
	CALL	SKIP_SPACES
	JP	C,.ERR
	LD	DE,OUT_FILE
	LD	C,LOCAL_FILE_SIZE-1
	CALL	COPY_ARG
	JP	C,.ERR

.OPTIONS
	CALL	SKIP_SPACES
	JR	C,.DEFAULT_NAMES
	LD	DE,ARG2_BUFF
	LD	C,URL_SIZE-1
	CALL	COPY_ARG
	JP	C,.ERR
	PUSH	HL
	PUSH	BC
	CALL	IS_ARG2_OUTPUT
	POP	BC
	POP	HL
	JR	NC,.OUTPUT
	PUSH	HL
	PUSH	BC
	CALL	IS_ARG2_YES
	POP	BC
	POP	HL
	JR	NC,.YES
	PUSH	HL
	PUSH	BC
	CALL	IS_ARG2_HELP
	POP	BC
	POP	HL
	JR	NC,.HELP
	JR	.ERR
.OUTPUT
	CALL	SKIP_SPACES
	JR	C,.ERR
	LD	DE,ARG3_BUFF
	LD	C,URL_SIZE-1
	CALL	COPY_ARG
	JR	C,.ERR
	LD	A,(TRANSFER_MODE)
	AND	A
	JR	NZ,.PUT_REMOTE
	LD	HL,ARG3_BUFF
	LD	DE,OUT_FILE
	LD	C,LOCAL_FILE_SIZE-1
	CALL	COPY_ASCIIZ_LIMIT
	JR	NC,.OPTIONS
	JR	.ERR
.PUT_REMOTE
	LD	HL,ARG3_BUFF
	LD	DE,REMOTE_FILE
	LD	C,REMOTE_FILE_SIZE-1
	CALL	COPY_ASCIIZ_LIMIT
	JR	NC,.OPTIONS
	JR	.ERR
.YES
	LD	A,1
	LD	(FORCE_OVERWRITE),A
	JR	.OPTIONS
.DEFAULT_NAMES
	LD	A,(TRANSFER_MODE)
	AND	A
	JR	NZ,.DEFAULT_PUT_REMOTE
	LD	A,(OUT_FILE)
	AND	A
	CALL	Z,BUILD_LOCAL_FROM_REMOTE
	RET	NC
	JR	.ERR
.DEFAULT_PUT_REMOTE
	LD	A,(REMOTE_FILE)
	AND	A
	CALL	Z,BUILD_REMOTE_FROM_LOCAL
	RET	NC
	JR	.ERR
.HELP
	LD	A,1
	LD	(HELP_REQUESTED),A
	RET
.ERR
	SCF
	RET

