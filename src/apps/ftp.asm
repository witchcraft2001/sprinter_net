		LD	DE,WIFI.RS_BUFF
		LD	BC,DEFAULT_TIMEOUT
		CALL	WIFI.UART_TX_CMD
		AND	A
		JR	Z,.OK
		PRINTLN MSG_RESETTING_ESP
		CALL	WIFI.ESP_RESET
		CALL	WIFI.UART_SET_DEFAULT_DIVISOR
		CALL	WIFI.UART_INIT
		POP	HL
		JP	SEND_CMD
.OK
		POP	HL
		RET

APPEND_STR
		LD	A,(DE)
		AND	A
		RET	Z
		LD	(HL),A
		INC	HL
		INC	DE
		JR	APPEND_STR

APPEND_IX_STR
